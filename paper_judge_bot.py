def _discord_chunks(msg: str, limit: int = 1990) -> list[str]:
    """Split a message into Discord-safe chunks (<=2000 chars each).
    Tries to break on newline boundaries; falls back to hard slicing."""
    if len(msg) <= limit:
        return [msg]
    chunks = []
    remaining = msg
    while len(remaining) > limit:
        cut = remaining.rfind("\n", 0, limit)
        if cut < int(limit * 0.5):
            cut = limit
        chunks.append(remaining[:cut])
        remaining = remaining[cut:].lstrip("\n")
    if remaining:
        chunks.append(remaining)
    return chunks


def discord_send(msg: str) -> None:
    """Send a Discord notification via the configured channel.

    Picks the first available transport:
      1. DISCORD_WEBHOOK_URL — webhook in the target channel.
      2. DISCORD_BOT_TOKEN + DISCORD_CHANNEL_ID — Discord bot REST API.

    Long messages are split into multiple POSTs (Discord's 2000-char limit).
    Failures are logged but never raised — Discord outage shouldn't kill the bot.
    """
    try:
        import httpx
    except ImportError:
        log.warning("discord skipped: httpx not installed")
        return

    chunks = _discord_chunks(msg)
    if config.DISCORD_WEBHOOK_URL:
        for c in chunks:
            try:
                httpx.post(config.DISCORD_WEBHOOK_URL, json={"content": c}, timeout=5.0)
            except Exception as e:
                log.warning("discord webhook send failed: %s", e)
                return
        return

    if config.DISCORD_BOT_TOKEN and config.DISCORD_CHANNEL_ID:
        url = f"https://discord.com/api/v10/channels/{config.DISCORD_CHANNEL_ID}/messages"
        for c in chunks:
            try:
                httpx.post(
                    url,
                    json={"content": c},
                    headers={
                        "Authorization": f"Bot {config.DISCORD_BOT_TOKEN}",
                        "Content-Type": "application/json",
                    },
                    timeout=5.0,
                )
            except Exception as e:
                log.warning("discord bot send failed: %s", e)
                return
        return

    log.debug("discord skipped (no transport configured): %s", msg[:120])


def _install_crash_alerts() -> None:
    """Route uncaught exceptions (main thread + background threads) to Discord (throttled)."""
    import sys as _sys, traceback as _tb, os as _os, time as _t, threading as _th
    _label = _os.path.basename(_os.path.dirname(_os.path.abspath(__file__))) or "bot"
    _last = [0.0]
    def _emit(where, et, ev, tb):
        now = _t.time()
        if now - _last[0] < 60:
            return
        _last[0] = now
        try:
            tbstr = "".join(_tb.format_exception(et, ev, tb))[-1500:]
            discord_send("CRASH [%s] uncaught in %s:\n```\n%s\n```" % (_label, where, tbstr))
        except Exception:
            pass
    _prev = _sys.excepthook
    def _hook(et, ev, tb):
        _emit("main", et, ev, tb)
        _prev(et, ev, tb)
    _sys.excepthook = _hook
    def _thook(a):
        _emit("thread:%s" % getattr(a.thread, "name", "?"), a.exc_type, a.exc_value, a.exc_traceback)
        _th.__excepthook__(a)
    _th.excepthook = _thook


