def lst_close_ts(station: str, climate_date: str) -> Optional[float]:
    """Return UTC ts of climate-day close. Uses LST (no DST) per Kalshi settlement."""
    tz_name = STATION_TZ.get(station)
    if not tz_name:
        return None
    try:
        tz = ZoneInfo(tz_name)
        # Use Jan-15 trick to get the LST offset for the year.
        y = int(climate_date.split("-")[0])
        lst_offset_h = datetime(y, 1, 15, tzinfo=tz).utcoffset().total_seconds() / 3600.0
        d = datetime.strptime(climate_date, "%Y-%m-%d")
        start_utc = d.replace(tzinfo=timezone.utc) + timedelta(hours=-lst_offset_h)
        end_utc = start_utc + timedelta(hours=24)
        return end_utc.timestamp()
    except Exception as e:
        log.warning("lst_close_ts %s %s: %s", station, climate_date, e)
        return None


