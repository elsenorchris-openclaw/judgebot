def _topup_is_safe(cand, packet: dict, pos: dict) -> tuple[bool, str]:
    """Top-up-only safety filters. Stricter than entry prescreen because
    there is no LLM judgment layer to catch a stale-forecast vs collapsed-
    market mismatch. Returns (ok, reason)."""
    side = pos.get("action")
    if side not in ("BUY_NO", "BUY_YES"):
        return False, f"unknown side {side}"

    # F-RM-STALE (mirror of entry-path gate): refuse top-ups when bot's rm
    # disagrees with live wethr 30-min window by >2°F. Same mechanism as the
    # post-LLM gate in run_entry_loop. Catches wethr-cache refresh-race bugs
    # where packet's rm lags live readings (NY 5/15 was the trigger case).
    _rm_t = packet.get("running_min_or_max")
    if _rm_t is not None:
        _wo_t = packet.get("wethr_obs") or {}
        _hist_t = scache.get_temp_history(cand.station, lookback_sec=1800.0) or []
        _ht = [p.get("temp_f") for p in _hist_t if p.get("temp_f") is not None]
        _live_t = _wo_t.get("temp_f")
        if cand.series_prefix == "KXHIGH":
            _cv = [t for t in (_live_t, _wo_t.get("high_f"), *_ht) if t is not None]
            _le = max(_cv) if _cv else None
            if _le is not None and (_le - _rm_t) > 2.0:
                return False, f"rm_stale: rm={_rm_t:.1f} vs live max={_le:.1f}"
        else:
            _cv = [t for t in (_live_t, _wo_t.get("low_f"), *_ht) if t is not None]
            _le = min(_cv) if _cv else None
            if _le is not None and (_rm_t - _le) > 2.0:
                return False, f"rm_stale: rm={_rm_t:.1f} vs live min={_le:.1f}"

    no_ask = packet.get("no_ask_c"); no_bid = packet.get("no_bid_c")
    yes_ask = packet.get("yes_ask_c"); yes_bid = packet.get("yes_bid_c")
    if side == "BUY_NO":
        ask_c, bid_c = no_ask, no_bid
    else:
        ask_c, bid_c = yes_ask, yes_bid
    if ask_c is None or bid_c is None:
        return False, "missing current bid/ask"

    # Absolute price floors — never buy a side the market values near zero.
    if ask_c < 25:
        return False, f"side ask {ask_c}c < 25c (market consensus against)"
    if bid_c < 10:
        return False, f"side bid {bid_c}c < 10c (market won't pay for our side)"

    # Price deterioration vs entry — thesis broke if market repriced hard.
    try:
        entry_c = int(round(float(pos.get("entry_price") or 0) * 100))
    except (TypeError, ValueError):
        entry_c = 0
    if entry_c > 0 and ask_c < entry_c * 0.75:
        return False, f"ask {ask_c}c < 75% of entry {entry_c}c (market revised against)"

    # Edge sanity cap — suspiciously large model-market gap means stale
    # forecast or unprocessed market info; do not top up into that.
    edge_info = packet.get("_edge_info") or {}
    edge = edge_info.get("edge")
    if edge is not None and edge > 0.30:
        return False, f"edge {edge:.2f} > 0.30 (likely stale forecast / dislocation)"

    # Forecast must still favor our side. If the bot's numerical model now
    # thinks the OTHER side has edge, the original LLM decision is no longer
    # supported by current data.
    edge_side = edge_info.get("side")
    if edge_side and edge_side != side:
        return False, f"forecast favors {edge_side} but position is {side}"

    # Obs trajectory — running max/min has entered the losing zone, OR
    # is on the losing side past the typical extreme hour.
    #
    # Coverage map (after 2026-05-15 fix). B = both floor+cap set;
    # T-warm = only floor set; T-cold = only cap set.
    #
    #            BUY_NO loses if              BUY_YES loses if
    #   HIGH-B   rm in/past bracket           rm > cap
    #   HIGH-Tw  rm > floor                   rm <= floor (post-peak)
    #   HIGH-Tc  rm < cap (post-peak)         rm > cap
    #   LOWT-B   rm in/past bracket           rm < floor
    #   LOWT-Tw  rm > floor (post-min)        rm < floor
    #   LOWT-Tc  rm < cap                     rm > cap (post-min)
    #
    # Pre-fix code only handled the 4 cases where the boundary field for
    # that direction was set — silently passed top-ups on the other 4
    # shapes. The four NEW post-peak/post-min checks below close those gaps.
    rm = packet.get("running_min_or_max")
    cap = packet.get("cap"); floor = packet.get("floor")
    series = cand.series_prefix
    clk = packet.get("local_clock") or {}
    past_peak = bool(clk.get("past_peak_today"))
    past_min = bool(clk.get("past_min_today"))
    if rm is not None:
        if side == "BUY_NO":
            if series == "KXHIGH":
                # HIGH-B and HIGH-Tw (both have floor set): rm at/past floor.
                if floor is not None and rm >= floor:
                    return False, (
                        f"HIGH BUY_NO at risk: running_max {rm:.1f} >= floor {floor:.1f}"
                    )
                # HIGH-Tc (only cap set): past peak with rm < cap means HIGH
                # didn't reach cap → BUY_NO will lose (HIGH < cap = YES).
                if floor is None and cap is not None and past_peak and rm < cap:
                    return False, (
                        f"HIGH BUY_NO cold-tail past peak: running_max {rm:.1f} < cap {cap:.1f}"
                    )
            else:  # KXLOWT
                # LOWT-B and LOWT-Tc (both have cap set): rm at/below cap.
                if cap is not None and rm <= cap:
                    return False, (
                        f"LOWT BUY_NO at risk: running_min {rm:.1f} <= cap {cap:.1f}"
                    )
                # LOWT-Tw (only floor set): past min with rm > floor means LOW
                # didn't drop low enough → BUY_NO will lose (LOW > floor = YES).
                if cap is None and floor is not None and past_min and rm > floor:
                    return False, (
                        f"LOWT BUY_NO warm-tail past min: running_min {rm:.1f} > floor {floor:.1f}"
                    )
        else:  # BUY_YES
            if series == "KXHIGH":
                # HIGH-B and HIGH-Tc (both have cap set): rm above cap.
                if cap is not None and rm > cap:
                    return False, (
                        f"HIGH BUY_YES lost: running_max {rm:.1f} > cap {cap:.1f}"
                    )
                # HIGH-Tw (only floor set): past peak with rm <= floor means
                # HIGH didn't climb past floor → BUY_YES loses (needs HIGH > floor).
                if cap is None and floor is not None and past_peak and rm <= floor:
                    return False, (
                        f"HIGH BUY_YES warm-tail past peak: running_max {rm:.1f} <= floor {floor:.1f}"
                    )
            else:  # KXLOWT
                # LOWT-B and LOWT-Tw (both have floor set): rm below floor.
                if floor is not None and rm < floor:
                    return False, (
                        f"LOWT BUY_YES lost: running_min {rm:.1f} < floor {floor:.1f}"
                    )
                # LOWT-Tc (only cap set): past min with rm > cap means LOW
                # didn't drop past cap → BUY_YES loses (needs LOW < cap).
                if floor is None and cap is not None and past_min and rm > cap:
                    return False, (
                        f"LOWT BUY_YES cold-tail past min: running_min {rm:.1f} > cap {cap:.1f}"
                    )

    return True, "ok"


def _process_topups(rt: "Runtime", candidates: list) -> int:
    """Code-only top-ups for held tickers with cumulative cost < min_buy_usd.

    For each paper-judge-owned position with cumulative cost below the
    min_buy floor and a climate_day equal to today, find the matching
    Candidate (with fresh market data), build a packet, and run prescreen.
    If prescreen passes, place an additional buy on the SAME ticker using
    the original action — no LLM call.

    Returns count of top-ups attempted.
    """
    min_buy = config.GUARDRAILS.get("min_buy_usd", 0.0)
    if min_buy <= 0:
        return 0
    today = guardrails.today_utc(time.time())
    todo: dict = {}
    for tk, pos in rt.positions.items():
        if pos.get("opened_by") != "paper-judge":
            continue
        if pos.get("date_str") != today:
            continue
        cc = rt.ctx.ticker_total_cost_usd.get(tk, 0.0)
        if 0 < cc < min_buy:
            todo[tk] = pos
    if not todo:
        return 0
    log.info("top-up pass: %d candidate(s) below $%.2f min", len(todo), min_buy)
    # Match against the fully-populated candidate list (has live market data).
    cand_by_ticker = {c.ticker: c for c in candidates}
    n_attempted = 0
    for tk, pos in todo.items():
        cand = cand_by_ticker.get(tk)
        if cand is None:
            log.info("top-up SKIP %s: no matching open candidate this cycle", tk)
            continue
        packet = build_entry_packet(rt, cand)
        if packet is None:
            log.info("top-up SKIP %s: no packet", tk)
            continue
        rej = prescreen(packet)
        if rej:
            log.info("top-up SKIP %s: prescreen %s", tk, rej)
            continue

        # Top-up-only safety filters — stricter than entry prescreen because
        # there is no LLM sanity check at this stage.
        safe_ok, safe_reason = _topup_is_safe(cand, packet, pos)
        if not safe_ok:
            log.info("top-up SKIP %s: safety %s", tk, safe_reason)
            discord_send(
                f"🛑 **TOP-UP BLOCKED** `{tk}` — {safe_reason}"
            )
            continue

        # Synthesize a minimal EntryDecision with the original action.
        # No LLM call; quality gates are bypassed via is_topup=True.
        action = pos.get("action") or "BUY_NO"
        orig_judge = pos.get("judge") or {}
        synth = judgment.EntryDecision(
            decision=action,
            conviction=float(orig_judge.get("conviction") or 1.0),
            size_factor=1.0,
            read="(code-only top-up; inherited from prior LLM entry)",
            key_risks=list(orig_judge.get("key_risks") or []),
            what_would_change_my_mind="",
            parse_ok=True,
            parse_error=None,
            input_tokens=0,
            output_tokens=0,
            cached_input_tokens=0,
            est_cost_usd=0.0,
            elapsed_sec=0.0,
        )
        n_attempted += 1
        execute_buy(rt, cand, packet, synth, is_topup=True)
    return n_attempted


def _resolve_settlements(rt: "Runtime") -> int:
    """Detect newly-settled markets and append outcome rows to today's
    settlements.jsonl. Joins to trades.jsonl by ticker — produces the
    "did our BUY win?" answer per trade. Called once per cycle.

    Tracks which tickers we've already logged via rt._settled_logged.
    On first call after restart, seeds the set from today's settlements
    file so we don't re-log every still-active settled position (fixed
    2026-05-15 — was causing ~5x duplication after restarts)."""
    if not hasattr(rt, "_settled_logged"):
        rt._settled_logged = state.load_settled_tickers_today()
        if rt._settled_logged:
            log.info("seeded _settled_logged with %d tickers from today's settlements.jsonl",
                     len(rt._settled_logged))
    # Universe of tickers we care about: positions or wallet-held that are
    # PAST or AT their climate day. Future-dated tickers can't have settled
    # yet — skipping them saves Kalshi calls (~50 positions × 96 cycles/day).
    today = guardrails.today_utc(time.time())
    of_interest: set[str] = set()
    for tk in (set(rt.positions.keys()) | set(rt.wallet_held_tickers)):
        pos = rt.positions.get(tk) or {}
        cday = pos.get("date_str")
        if cday is None:
            # Position not in our records (foreign-bot ticker) — parse from ticker.
            try:
                c = market_universe.parse_ticker(tk)
                cday = c.climate_day if c else None
            except Exception:
                cday = None
        if cday is not None and cday > today:
            continue  # future-dated, can't be settled yet
        of_interest.add(tk)
    if not of_interest:
        return 0
    n_new = 0
    for tk in of_interest:
        if tk in rt._settled_logged:
            continue
        try:
            m = kalshi_client.get_market(tk)
        except Exception:
            continue
        mk = m.get("market", m) if isinstance(m, dict) else {}
        status = (mk or {}).get("status") or ""
        result = (mk or {}).get("result") or ""
        # Settled markets show result ∈ {"yes","no"} or "" still; status may
        # be "settled" / "finalized" / "closed" depending on Kalshi state.
        if status not in ("settled", "finalized") and result not in ("yes", "no"):
            # Use last_price as a proxy if status is closed and book is empty.
            # Tighten threshold (2026-05-15): only classify when price is at
            # the extreme (≤0.10 or ≥0.90). Mid-range means market never
            # crystallized — wait for an explicit result rather than guess.
            last = (mk or {}).get("last_price_dollars")
            try:
                last_f = float(last) if last is not None else None
            except (TypeError, ValueError):
                last_f = None
            if status != "closed" or last_f is None:
                continue
            if last_f >= 0.90:
                inferred = "yes"
            elif last_f <= 0.10:
                inferred = "no"
            else:
                continue  # ambiguous — defer to explicit result later
        else:
            # 2026-05-16 cleanup: `result` is guaranteed non-empty here — we
            # entered this `else` branch precisely because `result in ("yes","no")`
            # OR `status in ("settled","finalized")`; in the latter case `result`
            # is the canonical settled value. The old `expiration_value` fallback
            # was unreachable. Default to "no" only if `result` is somehow empty.
            inferred = result or "no"
        pos = rt.positions.get(tk) or {}
        side = "no" if pos.get("action") == "BUY_NO" else ("yes" if pos.get("action") == "BUY_YES" else None)
        cost = float(pos.get("cost") or 0.0)
        cnt = int(pos.get("count") or 0)
        payout = 0.0
        if side == "no" and inferred == "no":
            payout = cnt * 1.0
        elif side == "yes" and inferred == "yes":
            payout = cnt * 1.0
        pnl = payout - cost if cnt > 0 else None
        rec = {
            "ts": time.time(),
            "ticker": tk,
            "status": status,
            "result": inferred,
            "had_position": cnt > 0,
            "action": pos.get("action"),
            "count": cnt,
            "cost_usd": cost if cnt > 0 else None,
            "payout_usd": payout if cnt > 0 else None,
            "pnl_usd": pnl,
            "station": pos.get("station"),
            "climate_day": pos.get("date_str"),
        }
        try:
            state.log_settlement(rec)
            n_new += 1
            rt._settled_logged.add(tk)
        except Exception:
            log.exception("settlement log failed for %s", tk)
    return n_new


