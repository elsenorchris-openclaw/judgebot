def run_exit_loop(rt: Runtime) -> None:
    """Parallel exit loop:
       Phase 1 (serial): build packets + check triggers (mostly local, cached).
       Phase 2 (parallel): Claude judge_exit on all triggered positions.
       Phase 3 (serial): log + execute_sell (state mutations must be serial).
    """
    if rt.ctx.kill_switch_active:
        return

    # Phase 1: trigger scan
    triggered: list[tuple[str, dict, dict, list[str]]] = []
    for ticker in list(rt.positions.keys()):
        if rt.shutdown:
            return
        pos = rt.positions[ticker]
        # Co-existence safety: NEVER sell a position we didn't open.
        if pos.get("opened_by") != "paper-judge":
            continue
        packet = build_exit_packet(rt, ticker, pos, trigger_reason="(scanning)")
        if not packet:
            continue
        mtm = packet.get("mtm_pct")
        if mtm is not None:
            pos["peak_mtm_pct"] = max(pos.get("peak_mtm_pct") or mtm, mtm)
            pos["trough_mtm_pct"] = min(pos.get("trough_mtm_pct") or mtm, mtm)
        # Skip exit Claude call when position is already near settlement.
        # bid_side_c >= 97c → we'd give up 3c+ of upside vs holding to settlement.
        # bid_side_c <= 3c → 3c is noise, hold for free optionality on reversal.
        bid_c = packet.get("bid_side_c")
        if bid_c is not None and (bid_c >= 97 or bid_c <= 3):
            continue
        triggers_list = exit_triggers(packet, pos)
        if not triggers_list:
            continue
        trigger_reason = ",".join(triggers_list)
        packet["trigger_reason"] = trigger_reason
        packet["triggered"] = True
        triggered.append((ticker, pos, packet, triggers_list))

    if not triggered:
        return

    # Circuit-breaker gate: skip exit-LLM dispatch when paused. Positions
    # stay held (same outcome as parse_ok=False fall-through) but we don't
    # burn API calls during an outage. See entry-loop gate for context.
    if rt.ctx.now_utc < rt.ctx.llm_paused_until_ts:
        pause_remaining = rt.ctx.llm_paused_until_ts - rt.ctx.now_utc
        log.warning("exit loop SKIPPING LLM dispatch — circuit breaker active "
                    "for %.0fs more (%d triggered positions held)",
                    pause_remaining, len(triggered))
        discord_send(
            f"🛑 exit **SKIPPED** — LLM circuit-breaker active "
            f"({pause_remaining:.0f}s remaining). {len(triggered)} triggered "
            f"positions held until pause lifts."
        )
        return

    log.info("exit loop: %d positions triggered, dispatching Claude in parallel",
             len(triggered))

    from concurrent.futures import ThreadPoolExecutor, as_completed
    # Phase 2: parallel Claude calls
    workers = max(1, int(getattr(config, "EXIT_PARALLEL_WORKERS", 10)))
    decisions: dict[str, "judgment.ExitDecision"] = {}
    with ThreadPoolExecutor(max_workers=workers) as ex:
        futs = {
            ex.submit(judgment.judge_exit, pkt, position_count=int(pos.get("count") or 0)): tk
            for tk, pos, pkt, _ in triggered
        }
        for f in as_completed(futs):
            tk = futs[f]
            try:
                decisions[tk] = f.result()
            except Exception as e:
                log.warning("judge_exit %s failed: %s", tk, e)

    # Phase 3a (serial): log decisions + filter to actual SELL list.
    # Mutations of rt.ctx counters must be serial.
    sells_to_execute: list[tuple[str, dict, dict, "judgment.ExitDecision"]] = []
    for ticker, pos, packet, triggers_list in triggered:
        if rt.shutdown:
            return
        decision = decisions.get(ticker)
        if decision is None:
            continue
        guardrails.record_api_spend(rt.ctx, decision.est_cost_usd)
        state.log_decision({
            "kind": "exit_decision",
            "ticker": ticker,
            "triggers": triggers_list,
            "decision": decision.decision,
            "sell_count": decision.sell_count,
            "limit_price_cents": decision.limit_price_cents,
            "conviction": decision.conviction,
            "read": decision.read,
            "regret_check": decision.regret_check,
            "parse_ok": decision.parse_ok,
            "parse_error": decision.parse_error,
            "input_tokens": decision.input_tokens,
            "output_tokens": decision.output_tokens,
            "cached_input_tokens": decision.cached_input_tokens,
            "est_cost_usd": decision.est_cost_usd,
        })
        state.record_shared_decision(
            stage="exit", decision=decision.decision, ticker=ticker,
            station=pos.get("station"), floor=pos.get("floor"), cap=pos.get("cap"),
            llm_conviction=decision.conviction,
        )
        if not decision.parse_ok:
            guardrails.record_llm_failure(rt.ctx, config.GUARDRAILS["llm_failure_pause_sec"])
            continue
        else:
            guardrails.record_llm_success(rt.ctx)
        if decision.decision == "HOLD":
            continue
        sells_to_execute.append((ticker, pos, packet, decision))

    # Phase 3b (parallel): execute_sell across all SELL decisions.
    # Kalshi place_sell is the slow part (HTTP + wait_for_fill). State writes
    # inside execute_sell are protected by serializing the final persist.
    if not sells_to_execute:
        return
    log.info("exit loop: %d sells dispatching in parallel", len(sells_to_execute))
    workers = max(1, int(getattr(config, "EXIT_PARALLEL_WORKERS", 10)))
    with ThreadPoolExecutor(max_workers=workers) as ex:
        futs = [
            ex.submit(execute_sell, rt, tk, pos, pkt, dec)
            for tk, pos, pkt, dec in sells_to_execute
        ]
        for f in as_completed(futs):
            try:
                f.result()
            except Exception as e:
                log.warning("execute_sell failed: %s", e)
    # Single persist at the end captures all the in-memory mutations.
    rt.persist_positions()


def execute_sell(rt: Runtime, ticker: str, pos: dict, packet: dict,
                 decision: judgment.ExitDecision) -> None:
    action = pos.get("action")
    side = "no" if action == "BUY_NO" else "yes"
    sd = guardrails.SellDecision(
        ticker=ticker,
        side=side,
        count=decision.sell_count,
        limit_price_cents=decision.limit_price_cents or 1,
        seconds_to_close=packet.get("seconds_to_close") or 0,
        triggered=True,
    )
    ok, reason = guardrails.check_sell(rt.ctx, sd, config.GUARDRAILS)
    if not ok:
        log.info("SELL REJECTED %s: %s", ticker, reason)
        state.log_decision({"kind": "sell_rejected", "ticker": ticker, "reason": reason})
        return
    order_resp = kalshi_client.place_sell(ticker, side, sd.count, sd.limit_price_cents)
    if not order_resp:
        return
    order_id = order_resp["order_id"]
    filled = order_resp.get("filled", 0)
    status = order_resp.get("status", "?")
    if filled <= 0:
        status, filled = kalshi_client.wait_for_fill(order_id, sd.count)
    if filled <= 0:
        log.info("SELL %s not filled (status=%s)", ticker, status)
        kalshi_client.cancel_order(order_id)
        return
    revenue = filled * (sd.limit_price_cents / 100.0)
    remaining = state.record_exit(rt.positions, ticker, filled, revenue, reason="judge_exit")
    cost_basis = filled * float(pos.get("entry_price") or 0)
    pnl = revenue - cost_basis
    guardrails.record_sell(
        rt.ctx, ticker, realized_pnl_usd=pnl,
        full_close=(remaining is None),
    )
    if remaining is None:
        # Full close — release the foreign-bot claim so V2 max/min may adopt
        # if Kalshi somehow leaves a residual position. (Settlement payouts
        # can lag a few minutes; sync_claims_with_positions at startup will
        # also self-heal.)
        state.release_ticker(ticker)
    rt.persist_positions()
    discord_send(
        f"🔻 **{decision.decision}** {filled}x @ {sd.limit_price_cents}c on `{ticker}` "
        f"(pnl ${pnl:+.2f}, conv={decision.conviction:.2f}): {decision.read}"
    )


