def _is_rm_locked_for_side(packet: dict) -> tuple[bool, str]:
    """Is the outcome physically locked by running_min_or_max for the bot's
    chosen side? Returns (locked, reason).

    Extracted from prompt's RULE #2 EXCEPTION ("running-extreme lock bypass").
    Locks fire when:

      HIGH BUY_NO:
        - rm >= cap + 1.0 (B-bracket / T-cold tail) — HIGH already overshot;
          NO wins by stays-above-bracket
        - rm <= floor - 1.0 AND past peak — HIGH never reached floor; NO
          wins by stays-below-bracket (only HIGH-B/T-warm)
      HIGH BUY_YES:
        - B-bracket: rm in [floor-0.5, cap+0.5] AND past peak (already in YES)
        - T-warm tail (cap=None): rm >= floor + 1.0 (already crossed warm)
      LOW BUY_NO:
        - rm <= floor - 1.0 — LOW already below; NO wins by stays-below
        - rm >= cap + 1.0 AND past min — LOW never reached cap; NO wins
          by stays-above (only LOW-B/T-cold)
      LOW BUY_YES:
        - B-bracket: rm in [floor-0.5, cap+0.5] AND past min
        - T-cold tail (floor=None): rm <= cap - 1.0 (already crossed cold)

    Note: this function takes the side from packet["_edge_info"]["side"]. If
    edge_info is absent or rm is None, returns (False, "no_lock_check").
    """
    edge_info = packet.get("_edge_info")
    if not edge_info:
        return (False, "no_edge_info")
    side = edge_info.get("side")
    rm = packet.get("running_min_or_max")
    if rm is None:
        return (False, "rm_none")
    fl = packet.get("floor")
    cp = packet.get("cap")
    series = packet.get("series") or ""
    clk = packet.get("local_clock") or {}
    past_peak = bool(clk.get("past_peak_today"))
    past_min = bool(clk.get("past_min_today"))

    if series == "KXHIGH":
        if side == "BUY_NO":
            if cp is not None and rm >= cp + 1.0:
                return (True, f"HIGH BUY_NO overshoot: rm {rm} >= cap+1 ({cp+1})")
            if fl is not None and rm <= fl - 1.0 and past_peak:
                return (True, f"HIGH BUY_NO stays-below: rm {rm} <= floor-1 "
                              f"({fl-1}) AND past peak")
            return (False, "no_high_buyno_lock")
        elif side == "BUY_YES":
            if fl is not None and cp is not None:
                if past_peak and (fl - 0.5) <= rm <= (cp + 0.5):
                    return (True, f"HIGH BUY_YES B-bracket locked: rm {rm} in "
                                  f"[{fl-0.5},{cp+0.5}] AND past peak")
            elif fl is not None and cp is None and rm >= fl + 1.0:
                return (True, f"HIGH BUY_YES T-warm overshoot: rm {rm} >= "
                              f"floor+1 ({fl+1})")
            return (False, "no_high_buyyes_lock")
    elif series.startswith("KXLOW"):
        if side == "BUY_NO":
            if fl is not None and rm <= fl - 1.0:
                return (True, f"LOW BUY_NO stays-below: rm {rm} <= floor-1 ({fl-1})")
            if cp is not None and rm >= cp + 1.0 and past_min:
                return (True, f"LOW BUY_NO stays-above: rm {rm} >= cap+1 "
                              f"({cp+1}) AND past min")
            return (False, "no_low_buyno_lock")
        elif side == "BUY_YES":
            if fl is not None and cp is not None:
                if past_min and (fl - 0.5) <= rm <= (cp + 0.5):
                    return (True, f"LOW BUY_YES B-bracket locked: rm {rm} in "
                                  f"[{fl-0.5},{cp+0.5}] AND past min")
            elif cp is not None and fl is None and rm <= cp - 1.0:
                return (True, f"LOW BUY_YES T-cold crossed: rm {rm} <= cap-1 "
                              f"({cp-1})")
            return (False, "no_low_buyyes_lock")
    return (False, "unknown_series_or_side")


def _margin_outside_yes_f(
    mu: Optional[float],
    floor: Optional[float],
    cap: Optional[float],
    bracket_kind: Optional[str],
    side: Optional[str],
) -> Optional[float]:
    """Signed distance from μ to the nearest YES-window edge, in °F.

    Returns a value where:
      > 0  μ is outside the YES window in the direction that SUPPORTS `side`
      < 0  μ is inside the YES window (or outside in the wrong direction)
      None if required fields are missing

    B-bracket YES window: [floor − 0.5, cap + 0.5].
    T-warm (cap=None): YES if x > floor + 0.5.
    T-cold (floor=None): YES if x < cap − 0.5.
    """
    if mu is None or bracket_kind is None or side is None:
        return None
    if bracket_kind == "B":
        if floor is None or cap is None:
            return None
        yes_lo = floor - 0.5
        yes_hi = cap + 0.5
        if side == "BUY_NO":
            # NO wins when peak < yes_lo (undershoot) OR peak > yes_hi (overshoot)
            if mu < yes_lo:
                return yes_lo - mu       # μ supports undershoot
            if mu > yes_hi:
                return mu - yes_hi       # μ supports overshoot
            # μ inside YES — distance to nearest edge, but in the WRONG direction
            return -min(mu - yes_lo, yes_hi - mu)
        if side == "BUY_YES":
            if yes_lo <= mu <= yes_hi:
                # μ inside YES; the deeper inside, the higher the confidence
                return min(mu - yes_lo, yes_hi - mu)
            # μ outside YES — supports NO, against YES
            return -min(abs(yes_lo - mu), abs(mu - yes_hi))
    elif bracket_kind == "T":
        if floor is not None and cap is None:
            yes_edge = floor + 0.5
            if side == "BUY_NO":
                return yes_edge - mu     # >0 when μ below YES line
            if side == "BUY_YES":
                return mu - yes_edge
        if cap is not None and floor is None:
            yes_edge = cap - 0.5
            if side == "BUY_NO":
                return mu - yes_edge     # >0 when μ above YES line
            if side == "BUY_YES":
                return yes_edge - mu
    return None


# ─────────────────────────────────────────────────────────────────────────────
# Pre-screen (numerical filters before LLM call) — applied during entry loop
# ─────────────────────────────────────────────────────────────────────────────
def prescreen(packet: dict) -> Optional[str]:
    """Return None if the candidate passes pre-screen, else a rejection reason."""
    p = config.PRESCREEN
    spread = packet.get("spread_c") or 0
    if spread > p["max_spread_cents"]:
        return f"spread {spread}c > max {p['max_spread_cents']}c"
    yes_ask = packet.get("yes_ask_c") or 0
    no_ask = packet.get("no_ask_c") or 0
    # No "cheap side" filter — yes_ask + no_ask ≈ 100, so a 1c side just means
    # the OTHER side has 99c market conviction. That's high signal, not low.
    # Per-bet price limits are enforced in guardrails.check_buy after Claude
    # picks the side.
    # Market-confidence floor: skip undecided markets where neither side >= 60c.
    dom = max(yes_ask, no_ask)
    if dom < p.get("min_market_confidence_cents", 0):
        return f"market undecided: max ask {dom}c < {p['min_market_confidence_cents']}c"
    # Already-settled filter: when one side is at 99c+ the market is effectively
    # decided. Either we agree (1c upside, no edge) or disagree (99c risk for 1c
    # gain). Skip — saves a Claude call.
    max_settled = p.get("max_settled_threshold_cents", 99)
    if dom >= max_settled:
        return f"already settled: max ask {dom}c >= {max_settled}c"
    secs = packet.get("seconds_to_close")
    if secs is None:
        return "no climate-day close time"
    if secs < p["min_time_to_close_sec"]:
        return f"time-to-close {secs/60:.0f}min < min"
    if secs > p["max_time_to_close_sec"]:
        return f"time-to-close {secs/3600:.1f}h > max"
    # 2026-05-15: wethr-only policy. Strict gate: if wethr has no fresh temp
    # for this station, do not trade. NWS METAR staleness is no longer a gate
    # (replaced 97 KNYC blocks today; wethr's mesonet feed is sub-2min where
    # NWS METAR was 24–73min stale).
    wethr = packet.get("wethr_obs") or {}
    wx_age = wethr.get("age_sec")
    if wx_age is None:
        _wx_ts = wethr.get("ts") or wethr.get("obs_ts")
        if _wx_ts is not None:
            try:
                wx_age = max(0.0, time.time() - float(_wx_ts))
            except (TypeError, ValueError):
                wx_age = None
    if wethr.get("temp_f") is None or wx_age is None or wx_age > p["require_recent_obs_age_sec"]:
        return "no fresh wethr temp (wethr-only policy)"
    # forecast freshness — at least one source available
    fresh = False
    for k in ("mu_nbp", "mu_nbm", "mu_hrrr", "mu_ecmwf"):
        if packet.get(k) is not None:
            fresh = True
            break
    if not fresh:
        return "no recent forecast in shared cache"

    # NOTE: previous wethr_highest_probable_f vs rm sanity filter rolled back
    # 2026-05-15 — semantic misread on my part. `highest_probable_f` is wethr's
    # *forward-looking* probable range (next ~1h window), not a daily-max
    # prediction. Comparing forward-window probable to today's running max
    # over-fires constantly post-peak (rm captured the past high, wethr_proj
    # tracks current temp + small window). The real catches were 6°F+ outliers
    # but at 1.5°F threshold the filter would have choked legitimate trades.

    # Numerical edge floor — require ≥ min_numerical_edge on one side.
    edge_info = _numerical_edge(packet, p.get("default_sigma_f", 2.5))
    if edge_info is None:
        packet["_edge_info"] = None
    else:
        packet["_edge_info"] = edge_info
        if edge_info["edge"] < p.get("min_numerical_edge", 0.10):
            return (f"edge {edge_info['edge']:+.2f} on {edge_info['side']} "
                    f"< {p.get('min_numerical_edge', 0.10):.2f}")

        # 2026-05-17: forecast-only μ gate. When neither nn_match nor the
        # rm-anchored methods (anchored / low_rm_ceiling) produced a μ, the
        # bot would otherwise fall to a pure-forecast μ (best_mae_*, consensus_
        # median, raw_median). The bot's stated edge is observations; trading
        # on forecast-only μ is taking publicly priced signal as edge. See
        # config.PRESCREEN["skip_forecast_only_mu"] for backtest rationale.
        # 2026-05-18: STRICTER successor — skip_unless_nn_match blocks
        # EVERY non-nn_match μ (also catches `anchored` + `low_rm_ceiling`,
        # which the LLM was BUYing 100% of the time in violation of the
        # prompt's "nn_match-only" SKIP rule). Live data 48h: 3/3
        # low_rm_ceiling dispatches → 3 rule-violating BUYs. When
        # skip_unless_nn_match is True it supersedes skip_forecast_only_mu.
        mm = edge_info.get("mu_method") or ""
        if p.get("skip_unless_nn_match", False):
            if not mm.startswith("nn_match"):
                return f"non-nn_match μ ({mm[:40] or '(empty)'}) — strategy requires nn_match edge"
        elif p.get("skip_forecast_only_mu", False):
            if (mm.startswith("best_mae_")
                    or mm.startswith("consensus_median")
                    or mm.startswith("raw_median")):
                return f"forecast-only μ ({mm[:40]}) — no obs-anchored projection"

        # 2026-05-19: μ-margin filter. Block trades where the bot's projection
        # is too close to (or inside) the YES window for the chosen side to
        # be a confident bet. Live failure that motivated this:
        #   KXHIGHPHIL-26MAY18-B96.5  BUY_NO @ 35c × 11  →  settled YES, lost $3.85
        #   μ=96.3, σ=2.10, YES=[95.5, 97.5], margin = -0.20  (μ INSIDE YES)
        # Backtest n=28 (5/16-5/18): baseline -$44.60 → filtered (k=1.5/1.0,
        # σ≤2.5) +$16.88. rm-lock bypasses (physical settlement). See
        # config.PRESCREEN["margin_*"] for thresholds + rationale.
        if p.get("margin_filter_enabled", False):
            sigma = packet.get("sigma_chosen") or packet.get("sigma")
            mu = packet.get("mu_chosen")
            side = edge_info.get("side")
            margin = _margin_outside_yes_f(
                mu,
                packet.get("floor"),
                packet.get("cap"),
                packet.get("bracket_kind"),
                side,
            )
            if (sigma is not None and margin is not None
                    and isinstance(sigma, (int, float))
                    and isinstance(margin, (int, float))):
                # σ cap (independent of margin)
                sig_max = p.get("margin_max_sigma_f")
                if sig_max is not None and sigma > sig_max:
                    locked, _ = (_is_rm_locked_for_side(packet)
                                 if p.get("margin_filter_bypass_when_rm_locked", True)
                                 else (False, ""))
                    if not locked:
                        return (f"σ {sigma:.2f}°F > {sig_max:.2f}°F cap "
                                f"(uncertainty too wide for confident bracket bet)")
                # margin-vs-σ check
                k = (p.get("margin_k_sigma_buy_no", 1.5) if side == "BUY_NO"
                     else p.get("margin_k_sigma_buy_yes", 1.0))
                required = k * sigma
                if margin < required:
                    locked, _ = (_is_rm_locked_for_side(packet)
                                 if p.get("margin_filter_bypass_when_rm_locked", True)
                                 else (False, ""))
                    if not locked:
                        return (f"margin {margin:+.2f}°F < {k:.1f}σ "
                                f"({required:.2f}°F) for {side}; μ too close to "
                                f"YES window for confident bet")

    # ── 2026-05-16: RULE #2 — EV gap ceiling (extracted from prompt Step 10) ──
    # When market disagrees with our model by >60pp, market is likely right
    # (it has minute-level book + private inventory data). Bypass: if the
    # outcome is physically locked by `running_min_or_max` per
    # _is_rm_locked_for_side(), gap doesn't matter — the lock makes us right
    # mechanically. Mirrors the prompt's "EXCEPTION — running-extreme lock"
    # subsection that USED to live in Step 10.
    if edge_info is not None:
        gap = edge_info["edge"]
        max_gap = p.get("max_numerical_edge_gap", 0.60)
        if gap > max_gap:
            locked, lock_reason = _is_rm_locked_for_side(packet)
            if not locked:
                return (f"Rule#2: gap {gap*100:.0f}pp on {edge_info['side']} "
                        f"> {max_gap*100:.0f}pp without rm-lock ({lock_reason})")

    # μ-distance pre-gate — only fires when the OBVIOUS physics is ALREADY
    # priced into the dominant side (max ask ≥ 95c). Original 2026-05-15
    # version skipped on raw μ-distance alone, which killed 122/249 ≥10pp
    # BUY_NO opportunities over 2 days (e.g., 2026-05-16 KXHIGHDEN-B81.5:
    # mu=63 << floor=81, no_ask=75c → ~24pp edge, bot skipped). The original
    # comment ("market is locked, Claude has nothing to add") conflated "LLM
    # can't add insight" with "no edge exists" — but when market hasn't fully
    # priced the physics, that gap IS the edge. Edge-floor check above
    # already filters <6pp cases; this just protects against the truly
    # already-priced extremes (95c+) where transaction cost outweighs gap.
    # 2026-05-16 (Chris): patched per skip-analysis finding #1.
    if edge_info is not None:
        mu = edge_info["mu"]
        cap_v = packet.get("cap")
        floor_v = packet.get("floor")
        ya = packet.get("yes_ask_c") or 0
        na = packet.get("no_ask_c") or 0
        max_ask = max(ya, na)
        if max_ask >= 95:
            if cap_v is not None and mu > cap_v + 10.0:
                return (f"mu {mu:.1f} >> cap {cap_v:.1f} (>10F above) "
                        f"AND max_ask={max_ask}c ≥ 95c (already priced)")
            if floor_v is not None and mu < floor_v - 10.0:
                return (f"mu {mu:.1f} << floor {floor_v:.1f} (>10F below) "
                        f"AND max_ask={max_ask}c ≥ 95c (already priced)")

    # ── Time-of-day relevance (per-station local clock) ────────────────────
    # The bot's only edge is live obs informing the realized extreme.
    # Outside the windows below, the obs adds nothing the numerical bots
    # don't have — skip the LLM call.
    #
