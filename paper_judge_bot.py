# ─────────────────────────────────────────────────────────────────────────────
# Exit trigger predicates
# ─────────────────────────────────────────────────────────────────────────────
def exit_triggers(packet: dict, pos: dict) -> list[str]:
    """Return list of triggered predicates. Empty list = no trigger, HOLD."""
    triggered: list[str] = []
    mtm = packet.get("mtm_pct")
    peak = pos.get("peak_mtm_pct")
    if mtm is not None and peak is not None and (peak - mtm) > 0.30:
        triggered.append("mtm_swing")
    rm = packet.get("running_min_or_max")
    cap = packet.get("cap"); floor = packet.get("floor")
    action = pos.get("action")
    if rm is not None:
        if action == "BUY_NO" and cap is not None and abs(rm - cap) <= 1.5:
            triggered.append("rm_near_boundary")
        if action == "BUY_YES" and floor is not None and abs(rm - floor) <= 1.5:
            triggered.append("rm_near_boundary")
    secs = packet.get("seconds_to_close") or 0
    if secs < 90 * 60 and (pos.get("cost") or 0) > 20:
        triggered.append("time_to_close")
    obs = packet.get("live_obs") or {}
    temp = obs.get("temp_f")
    if temp is not None and rm is not None and abs(temp - rm) < 0.3 and secs < 3 * 3600:
        triggered.append("obs_anomaly")  # The DC pattern.
    spread = packet.get("spread_c") or 0
    if spread > 30:
        triggered.append("spread_wide")
    return triggered


