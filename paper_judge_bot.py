def one_cycle(rt: Runtime) -> None:
    rt.cycles += 1
    rt.hourly_cycles_run += 1
    rt.refresh_clock_and_flags()
    reconcile_positions_with_kalshi(rt)
    try:
        import low_post_probe
        low_post_probe.sweep(rt)
    except Exception as _lpe:
        log.warning("low_post sweep failed: %s", _lpe)
    # Subscribe position tickers to WS so build_exit_packet's BBO overlay has
    # fresh data when run_exit_loop runs immediately below. Idempotent —
    # candidate-loop subscribe later in the cycle covers the same tickers if
    # the position is also a tradable candidate.
    if USE_KALSHI_WS and rt.positions:
        try:
            kalshi_ws.subscribe(list(rt.positions.keys()))
        except Exception as _ws_pe:
            log.warning("kalshi_ws.subscribe (positions) failed: %s", _ws_pe)
    # 2026-05-19 push pure-code arch v2: also subscribe the FULL candidate
    # universe so push-side auto-execute receives BBO events for every
    # weather market (not just owned positions). Previously this happened
    # inside run_entry_loop; with LLM_DISPATCH_MODE=off we skip that loop
    # so we do it here unconditionally. Idempotent. kalshi_ws.subscribe
    # is a no-op for already-subscribed tickers.
    if USE_KALSHI_WS:
        try:
            _universe = market_universe.list_candidates()
            if _universe:
                kalshi_ws.subscribe([c.ticker for c in _universe])
                log.info("cycle %d: subscribed %d candidate tickers (universe)",
                         rt.cycles, len(_universe))
        except Exception as _ws_ue:
            log.warning("universe subscribe failed: %s", _ws_ue)
    # Wethr_rm degraded check — if wethr.net is down, halt trading and alert.
    if wethr_rm.is_degraded():
        mins = wethr_rm.degraded_seconds() / 60
        log.error("wethr_rm DEGRADED for %.1f min — skipping trading cycle", mins)
        discord_send(
            f"🚨 cycle {rt.cycles} SKIPPED — wethr.net down "
            f"{mins:.1f}min. running_max unavailable, not trading."
        )
        return
    cycle_start = time.time()
    log.info(
        "cycle %d  mode=%s  positions=%d  spend=$%.2f  api_spend=$%.2f  kill=%s",
        rt.cycles, rt.ctx.mode, rt.ctx.open_positions_count,
        rt.ctx.daily_spend_usd, rt.ctx.daily_api_spend_usd,
        rt.ctx.kill_switch_active,
    )
    if rt.ctx.mode == "killed":
        return
    # 2026-05-26: ENABLE_SELLS is True only for the adverse-drift exit (in the
    # push worker). The dormant LLM-era run_exit_loop stays OFF unless its own
    # flag is set — re-enabling sells must NOT silently revive it.
    if config.ENABLE_SELLS and getattr(config, "ENABLE_LLM_EXIT_LOOP", False):
        try:
            run_exit_loop(rt)
        except Exception:
            log.exception("exit loop error")
    # Settlement scanner — append outcomes to today's settlements.jsonl.
    # Lightweight: only hits Kalshi for tickers we hold or held.
    try:
        n_new = _resolve_settlements(rt)
        if n_new > 0:
            log.info("settlements logged: %d new", n_new)
    except Exception:
        log.exception("settlement resolver error")

    # 2026-05-19 push pure-code arch v2: when LLM is off, push owns trades.
    # Skip the heavy entry loop AND the loud Discord cycle-done post — main
    # cycle becomes quiet maintenance only (reconcile + settlements +
    # hourly summary), still ticking every EXIT_CYCLE_SEC (120s).
    _llm_mode = getattr(config, "LLM_DISPATCH_MODE", "all")
    if _llm_mode == "off":
        log.info("cycle %d: LLM_DISPATCH_MODE=off — entry loop skipped (push owns)",
                 rt.cycles)
    else:
        buys_at_start = rt.cycle_buys_count
        try:
            run_entry_loop(rt)
        except Exception:
            log.exception("entry loop error")
        cycle_elapsed = time.time() - cycle_start
        new_buys = rt.cycle_buys_count - buys_at_start
        discord_send(
            f"✅ cycle {rt.cycles} done in {cycle_elapsed:.0f}s — "
            f"{new_buys} new BUY(s), {rt.ctx.open_positions_count} positions, "
            f"day spend ${rt.ctx.daily_spend_usd:.2f}/${config.GUARDRAILS['daily_spend_cap_usd']:.0f}"
        )

    # Hourly summary — replaces the older heartbeat. Posts every
    # HOURLY_SUMMARY_SEC with cycle activity + wallet state.
    interval = config.GUARDRAILS.get("hourly_summary_sec", 3600)
    if rt.ctx.now_utc - rt.last_hourly_summary_ts >= interval:
        _emit_hourly_summary(rt)
        rt.last_hourly_summary_ts = rt.ctx.now_utc
        rt.last_heartbeat_ts = rt.ctx.now_utc


