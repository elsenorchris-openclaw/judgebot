def _extract_prob_from_read(read: str, prefer_side: str | None = None) -> tuple[str | None, float | None]:
    """Regex-extract Claude's stated P(NO)/P(YES) from a read.

    Returns (side, prob) where side ∈ {"BUY_NO", "BUY_YES", None}.
    If prefer_side is given (e.g., the bot's actual decision), the matching
    side's prob is returned even if it's < 0.55. Otherwise dominant signal.

    Regex requires the value to look like a probability — a DECIMAL form
    ("0.87", ".87") or PERCENT form ("87%", "87 percent"). Bare integers
    are REJECTED because Claude often writes math like:
        "P(NO) = 1−Φ((88.5−91.0)/2.24) ≈ 0.87"
    where the bare "1" is part of the formula 1−Φ(...), not the actual
    probability. Pre-2026-05-15 regex `0?\\.?(\\d{1,3})` greedily captured
    that "1" and normalized to 0.10 — wildly wrong. (Real bug:
    KXHIGHTDAL-T89, Claude prob 0.87 → extractor returned 0.10.)

    Search scans the WHOLE read for valid prob expressions (not just
    immediately after P(NO)) so reads like "P(NO) = formula ≈ 0.87" still
    find the 0.87. Last match wins (assumed to be Claude's final answer).
    """
    import re
    text = read or ""
    # PROB_VAL — a value that looks like a probability. Three forms:
    #   (a) decimal-percent "74.8%" — lookbehind [\d.] excludes both digit
    #       and period predecessors so e.g. "74.8%" isn't sliced into "8%".
    #   (b) decimal "0.87" or ".87" — REQUIRES non-digit before to reject
    #       captures inside larger numbers like "88.5" → ".5".
    #   (c) percent "87%" or "87 percent" — lookbehind [\d.] (not just \d)
    #       so e.g. "74.8%" doesn't fall through and match the bare "8%".
    # Bare integers ("1", "87") without % or decimal are NOT matched —
    # too easily mistaken for formula constants like the "1" in
    # "P(NO) = 1 − Φ(...) ≈ 0.87".
    PROB_RX = re.compile(
        r"(?<![\d.])(?P<decpct>\d{1,3}\.\d{1,2})\s*(?:%|percent\b)"
        r"|(?<!\d)(?P<dec>0?\.\d{1,3})"
        r"|(?<![\d.])(?P<pct>\d{1,3})\s*(?:%|percent\b)",
        re.IGNORECASE,
    )

    def _find_last_prob(label_pat):
        """For the LAST occurrence of `label` (P(NO) or P(YES)), scan
        from that position to the FIRST of:
          - next P(NO)/P(YES) marker (stop BEFORE it)
          - sentence-ending punctuation `. ` / `; ` / newline (stop AFTER)
          - end of text
        Return the LAST valid probability value in that bounded segment.

        2026-05-16: Added sentence-boundary stop. Without it the scan
        greedily picks up stray decimals from following clauses — e.g.
        "P(NO)≈0.944 vs market 81c = +13pp gap. 60m regression r²=0.52"
        returned 0.52 (the r²!) instead of 0.944, causing a false
        market_drift rejection at submit. `(?<!\\d)` on the punctuation
        keeps the boundary from triggering inside decimals like "88.5"
        so formula-then-answer ("P(NO) = 1−Φ(...) ≈ 0.87") still works.
        """
        # Find all matches of the label
        label_positions = [m.start() for m in re.finditer(label_pat, text, re.IGNORECASE)]
        if not label_positions:
            return None
        start = label_positions[-1]
        tail = text[start + len(label_pat):]
        # Candidate boundary 1: next P(NO)/P(YES) — stop BEFORE it.
        p_marker = re.search(r"P\((?:NO|YES)\)", tail, re.IGNORECASE)
        # Candidate boundary 2: sentence end — `.` or `;` not preceded
        # by a digit (avoids matching `88.5` mid-formula), followed by
        # whitespace OR end-of-string; or a literal newline. Stop AFTER
        # the punctuation so the value just before it is kept.
        sent_end = re.search(r"(?<!\d)[.;](?=\s|$)|\n", tail)
        bounds = []
        if p_marker is not None:
            bounds.append(p_marker.start())          # stop before next P()
        if sent_end is not None:
            bounds.append(sent_end.end())            # stop after sentence punct
        boundary = min(bounds) if bounds else len(tail)
        end = start + len(label_pat) + boundary
        segment = text[start:end]
        # Scan segment for valid probability values; take the LAST.
        last_val = None
        for m in PROB_RX.finditer(segment):
            dp = m.group("decpct")
            d = m.group("dec")
            p = m.group("pct")
            v: Optional[float] = None
            if dp is not None:
                try:
                    v = float(dp) / 100.0
                except ValueError:
                    continue
            elif d is not None:
                try:
                    v = float(d)
                except ValueError:
                    continue
            elif p is not None:
                try:
                    v = int(p) / 100.0
                except ValueError:
                    continue
            if v is not None and 0.0 < v < 1.0:
                last_val = v
        return last_val

    no_p = _find_last_prob(r"P\(NO\)")
    yes_p = _find_last_prob(r"P\(YES\)")
    if prefer_side == "BUY_NO" and no_p is not None:
        return "BUY_NO", no_p
    if prefer_side == "BUY_YES" and yes_p is not None:
        return "BUY_YES", yes_p
    if no_p is not None and no_p >= 0.55:
        return "BUY_NO", no_p
    if yes_p is not None and yes_p >= 0.55:
        return "BUY_YES", yes_p
    return None, None


def _gap_pp(prob: float, price_c: int) -> float:
    """Edge in percentage points: bot's prob minus market-implied prob (rounded 1dp)."""
    return round((prob - (price_c / 100.0)) * 100, 1)


def _claude_prob_for_side(decision: "judgment.EntryDecision", packet: dict,
                          side: str) -> tuple[Optional[float], str]:
    """Get the bot's best estimate of P(side wins) for sizing/edge checks.
    Returns (prob, source_label).

    Hierarchy:
      1. Claude's read — regex-extract P(NO)/P(YES) literal.
      2. Bot's _numerical_edge prob from prescreen (in packet._edge_info).
         If _edge_info.side matches our side, use prob directly; if it
         disagrees (Claude said BUY_NO but bot's edge said BUY_YES is best),
         invert: P(our side) = 1 − P(other side).
      3. None — no probabilistic signal available, caller should skip.
    """
    decision_kind = "BUY_NO" if side == "no" else "BUY_YES"
    _, p = _extract_prob_from_read(decision.read, prefer_side=decision_kind)
    if p is not None:
        # 2026-05-20: push pure-nn worker builds a synthetic EntryDecision whose
        # read string starts with 'pure-nn auto:' and embeds 'P(YES)=X.XX' from
        # the NN strategy — the regex above will match that literal, but it is
        # the NN-derived probability, not a Claude call. Distinguish the source.
        read_txt = decision.read or ""
        source = "nn_read" if read_txt.startswith("pure-nn auto") else "claude_read"
        return p, source
    ei = packet.get("_edge_info") or {}
    bot_prob = ei.get("prob")
    if bot_prob is None:
        return None, "no_source"
    try:
        bot_prob_f = float(bot_prob)
    except (TypeError, ValueError):
        return None, "no_source"
    if ei.get("side") == decision_kind:
        return bot_prob_f, "edge_info"
    return 1.0 - bot_prob_f, "edge_info_inverted"


def _scout_sweep_plan(
    ticker: str, side: str, claude_prob: float,
    target_cost: float, floor_cost: float,
    side_cap: float, balance_usd: float,
    max_price_cents: int, min_edge_pp: float,
) -> dict:
    """Read orderbook + plan the smallest sweep that reaches `target_cost`
    at edge ≥ `min_edge_pp` per level. Returns a plan dict:

      {
        "ok": bool,
        "qty": int,                    # contracts to submit
        "max_price_c": int or None,    # limit price for the sweep
        "expected_cum_cost": float,    # ≈ ceiling on $ we'd spend
        "expected_avg_c": float,       # ≈ average price per contract
        "edge_at_max_pp": float,       # edge at the deepest level taken
        "scan": list[(price_c, size, edge_pp)],  # for telemetry
        "skip_reason": str or None,
        "best_offers": list[(price_c, size)],    # first 5 offers for log
      }

    Gates per level (walked cheapest-first):
      - edge_pp < min_edge_pp        → stop (don't take any more)
      - price_c > max_price_cents    → stop
      - cum_cost would exceed side_cap or wallet balance → cap take
      - cum_cost reached target_cost → done (success)

    Final decision: ok = (cum_cost ≥ floor_cost), else skip with reason.
    """
    plan: dict = {
        "ok": False, "qty": 0, "max_price_c": None,
        "expected_cum_cost": 0.0, "expected_avg_c": None,
        "edge_at_max_pp": None, "scan": [], "skip_reason": None,
        "best_offers": [],
    }
    # 2026-05-16: WS-first orderbook. kalshi_ws maintains the full L2 ladder
    # from orderbook_delta; expose it here to skip one REST round-trip per buy
    # attempt. REST stays as fallback for stale/empty/inverted cache.
    ob: dict = {}
    ob_src = "rest"
    if USE_KALSHI_WS:
        try:
            ws_ob = kalshi_ws.get_orderbook(ticker)
        except Exception as _wse:
            ws_ob = None
            log.warning("kalshi_ws.get_orderbook %s err: %s", ticker, _wse)
        if ws_ob is not None:
            ob = ws_ob
            ob_src = "ws"
    if ob_src == "rest":
        try:
            ob = kalshi_client.get_orderbook(ticker)
        except Exception as e:
            plan["skip_reason"] = f"orderbook fetch failed: {type(e).__name__}"
            return plan
    plan["orderbook_src"] = ob_src
    offers = kalshi_client.offers_for_side(ob, side)
    plan["best_offers"] = offers[:5]
    if not offers:
        plan["skip_reason"] = "no offers on our side"
        return plan

    cum_qty = 0
    cum_cost = 0.0
    max_price_c: Optional[int] = None
    for price_c, size in offers:
        edge_pp = (claude_prob - price_c / 100.0) * 100.0
        plan["scan"].append((price_c, size, round(edge_pp, 1)))
        if edge_pp < min_edge_pp:
            break
        if price_c > max_price_cents:
            break
        # Side-cap + balance both clamp how much more we can spend.
        max_more_cost = min(side_cap - cum_cost, balance_usd - cum_cost)
        if max_more_cost <= 0:
            break
        remaining_target = target_cost - cum_cost
        if remaining_target <= 0:
            break  # already at target
        # Convert remaining $ to contracts (round up so we hit target).
        max_qty_target = max(1, int((remaining_target * 100 + price_c - 1) // price_c))
        max_qty_cap = int(max_more_cost * 100 // price_c)
        qty_at_level = min(size, max_qty_target, max_qty_cap)
        if qty_at_level <= 0:
            break
        cum_qty += qty_at_level
        cum_cost += qty_at_level * (price_c / 100.0)
        max_price_c = price_c

    if cum_qty > 0 and max_price_c is not None:
        plan["qty"] = cum_qty
        plan["max_price_c"] = max_price_c
        plan["expected_cum_cost"] = round(cum_cost, 4)
        plan["expected_avg_c"] = round(cum_cost / cum_qty * 100, 1)
        plan["edge_at_max_pp"] = round((claude_prob - max_price_c / 100.0) * 100, 1)

    if cum_qty == 0:
        plan["skip_reason"] = "no level passed edge gate"
    elif cum_cost < floor_cost:
        plan["skip_reason"] = (
            f"reachable ${cum_cost:.2f} ({cum_qty} contracts at edge ≥ "
            f"{min_edge_pp:.0f}pp) < floor ${floor_cost:.2f}"
        )
    else:
        plan["ok"] = True
    return plan


def _capture_shadow_trade(cand, decision, packet: dict, reason: str) -> dict | None:
    """Build a shadow-trade record for SKIPped or REJECTED markets where the
    AI signalled high conviction. Used for end-of-day analysis: "would the bot
    have won if we let it bet?" Detects intended side by regex-scanning the
    AI's read for P(NO)/P(YES) probability claims."""
    import time as _time
    side, prob = _extract_prob_from_read(decision.read)
    if not side:
        return None
    price_c = packet.get("no_ask_c") if side == "BUY_NO" else packet.get("yes_ask_c")
    if price_c is None:
        return None
    gap_pp = _gap_pp(prob, price_c)
    return {
        "ts": _time.time(),
        "ticker": cand.ticker,
        "kind": "shadow",
        "reason": reason,
        "would_action": side,
        "would_prob": round(prob, 3),
        "market_price_c": price_c,
        "gap_pp": gap_pp,
        "conviction": decision.conviction,
        "size_factor": decision.size_factor,
        "floor": cand.floor,
        "cap": cand.cap,
        "bracket_kind": cand.bracket_kind,
        "climate_day": cand.climate_day,
        "station": cand.station,
        "series": cand.series_prefix,
        "read": decision.read or "",
        # 2026-05-17: plumbing fix — these existed on EntryDecision but
        # weren't captured here, so scripts/r2_compare.py was reading
        # shadow_trades.jsonl with incomplete data.
        "key_risks": decision.key_risks,
        "what_would_change_my_mind": decision.what_would_change_my_mind,
        "obs_anchor": decision.obs_anchor,
        "obs_anchor_valid": decision.obs_anchor_valid,
        "obs_anchor_reason": decision.obs_anchor_reason,
    }


