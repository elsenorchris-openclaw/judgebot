def _price_c(m: dict, key_dollars: str) -> Optional[int]:
    """Read a Kalshi *_dollars field and convert to integer cents.
    Kalshi's current API returns prices as fractional dollars
    (e.g. yes_bid_dollars: 0.14). Internal code wants integer cents."""
    v = m.get(key_dollars)
    if v is None:
        return None
    try:
        return int(round(float(v) * 100))
    except (TypeError, ValueError):
        return None


def build_entry_packet(rt: Runtime, cand: market_universe.Candidate
                       ) -> Optional[dict]:
    """Compose the dynamic-context dict that gets passed to judge_entry.

    Reads from rt.cycle_data (prefilled once per cycle by live_data.prefetch).
    Returns None when essential data is missing — caller logs and skips.
    """
    m = cand.market
    yes_bid = _price_c(m, "yes_bid_dollars")
    yes_ask = _price_c(m, "yes_ask_dollars")
    no_bid = _price_c(m, "no_bid_dollars")
    no_ask = _price_c(m, "no_ask_dollars")
    if yes_ask is None and no_ask is None:
        return None
    yes_bid = yes_bid if yes_bid is not None else 0
    yes_ask = yes_ask if yes_ask is not None else 100
    no_bid = no_bid if no_bid is not None else 0
    no_ask = no_ask if no_ask is not None else 100
    spread = max(yes_ask - yes_bid, no_ask - no_bid)
    close_ts = lst_close_ts(cand.station, cand.climate_day)
    secs_to_close = (close_ts - rt.ctx.now_utc) if close_ts else None
    # Climate day starts at the same LST midnight, 24h before close_ts
    secs_since_climate_start = None
    climate_day_start_iso = None
    climate_day_end_iso = None
    if close_ts:
        start_ts = close_ts - 24 * 3600
        secs_since_climate_start = rt.ctx.now_utc - start_ts
        climate_day_start_iso = datetime.fromtimestamp(start_ts, tz=timezone.utc).isoformat()
        climate_day_end_iso = datetime.fromtimestamp(close_ts, tz=timezone.utc).isoformat()

    kind = "low" if cand.series_prefix == "KXLOW" else "high"
    sdata = live_data.get_station(rt.cycle_data, cand.station)
    fblock = live_data.get_forecast(rt.cycle_data, cand.station, cand.climate_day, kind)
    forecasts = fblock.get("sources") or {}
    nbp_extra = fblock.get("nbp_extra") or {}
    disag = fblock.get("disagreement_f")

    # running_min/max for THIS ticker's climate day.
    #
    # 2026-05-16 (F1): the previous check `cand.climate_day == today_utc` was
    # UTC-date-based, which silently passed for pre-LDT-midnight entries (e.g.
    # 2026-05-15T00:01 UTC = 2026-05-14 20:01 EDT for KATL → UTC date matches
    # but the LST climate day hasn't started yet → wethr cache low_f was the
    # PRIOR day's running min). 5/7 rm-having LOW losers last night cost
    # -$19.46 from this exact bug.
    #
    # Hardened validator requires ALL of:
    #   - wethr cache `date` field == cand.climate_day (string match)
    #   - time_of_low/high falls within DST-aware LDT window for climate_day
    #   - now >= LDT_midnight(climate_day) + 1h grace (cooling has established)
    # Falls back to rm_val=None on any failure. Stamps the verdict into the
    # packet so downstream code (prescreen, exit logic) sees why rm was nulled.
    import wethr_rm as _wrm
    _is_low = cand.series_prefix == "KXLOW"
    _time_of_x = (sdata.get("wethr_time_of_low_utc") if _is_low
                  else sdata.get("wethr_time_of_high_utc"))
    # 2026-05-16 (Chris): pre-dawn LOW d+0 only — shorten grace from 60→15 min.
    # pace_low_band / tail_low_band in the packet now give the LLM per-(station,
    # month) cooling-curve data so it self-skips early-overnight cases (the
    # 0-15min post-LST-midnight window where rm reflects evening temp, not
    # dawn min). The 60min grace was a blunt fallback from before that data.
    # Narrower window catches the most acute risk (0-15min when obs-pipeline
    # may not have a running_min row yet → fallback returns None → bot skips
    # naturally). For HIGH d+0 (post-LST-midnight = morning warming) keep
    # 60min — heating-curve data exists but the failure mode is different.
    # Late-evening d+0 LOW (cold-front case) is past 22h into climate day, so
    # this check never fires for that cohort regardless of grace_sec.
    _grace_sec = 900.0 if _is_low else 3600.0
    # 2026-05-16: prefer per-side LST date (wethr_date_low/_high), derived by
    # wethr-cache-service from time_of_*_utc. The legacy `wethr_running_date`
    # is wethr's lagging label (only flips at CLI ingest, 1-2 days late) and
    # caused systematic stale_cache_date false-positives in the first few
    # hours of every climate day. Fall back to the legacy field if the
    # derived value is absent (older cache writes or unknown-tz stations).
    _cache_date = (sdata.get("wethr_date_low") if _is_low
                   else sdata.get("wethr_date_high")) or sdata.get("wethr_running_date")
    rm_validation = _wrm.validate_rm_for_climate_day(
        station=cand.station,
        climate_day=cand.climate_day,
        cache_date=_cache_date,
        time_of_extreme_utc=_time_of_x,
        now_utc_ts=rt.ctx.now_utc,
        grace_sec=_grace_sec,
    )
    rm_val = None
    if rm_validation.get("ok"):
        rm_val = (sdata.get("running_min_today") if _is_low
                  else sdata.get("running_max_today"))
    else:
        log.info("rm validation FAIL %s climate_day=%s: %s",
                 cand.station, cand.climate_day, rm_validation.get("reason"))
        # 2026-05-16 (Chris): obs-pipeline fallback REMOVED — wethr-only policy.
        # On validation fail, rm_val stays None → prescreen "no rm anchor → SKIP"
        # blocks d+0 dispatch. pace_low_band / tail_low_band cover the pre-dawn
        # LOW d+0 case the fallback previously catered to.

    # FRESH obs per worker — wethr + NWS both have 60s caches AND the
    # obs_refresh background thread primes them every 90s/300s. So any
    # worker reading these gets data ≤ 60s old with zero latency.
    import wethr_client
    nws = obs_client.get_obs(cand.station)
    nws_obs = (
        {
            "temp_f": nws.temp_f, "dewpt_f": nws.dewpt_f,
            "sky": nws.sky, "wind_mph": nws.wind_mph,
            "trend_30m_f": nws.trend_30m_f, "ts_iso": nws.ts_iso,
            "age_sec": nws.age_sec, "source": nws.source,
        } if nws else sdata.get("nws_obs")
    )
    # 2026-05-15: wethr-only policy. Shared cache is the sole live-obs source.
    # If wethr-cache.service is degraded for this station, prescreen rejects
    # downstream (per user policy: no fresh wethr → don't trade). Dropped the
    # prior wethr_client.fetch_one() + sdata fallback chain because they
    # returned wethr_obs dicts without obs_ts/age_sec, which broke the strict
    # freshness gate. anomaly_f is no longer surfaced (the shared cache
    # doesn't carry it; Claude can derive from temp − climate_normal).
    wethr_obs = scache.get_wethr_obs(cand.station)
    clock = sdata.get("clock") or {}
    climate = sdata.get("climate") or {}
    hourly_fc = sdata.get("hourly_forecast_24h") or []
    afd = sdata.get("afd")
    mae = sdata.get("model_mae_high") if kind == "high" else sdata.get("model_mae_low")
