def _load_curves_file(path: str, label: str) -> dict:
    try:
        with open(path) as _f:
            curves = (json.load(_f) or {}).get("curves", {})
        log.info("loaded %s: %d series", label, len(curves))
        return curves
    except Exception as e:
        log.warning("%s load failed (%s): %s — that band will be None in packets",
                    label, path, e)
        return {}


