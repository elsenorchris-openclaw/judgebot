def _numerical_edge(packet: dict, default_sigma: float) -> Optional[dict]:
    """Compute the best directional edge (BUY_NO or BUY_YES) from forecast
    consensus + bracket boundaries. Returns dict with side / mp / edge, or
    None if forecasts aren't usable.

    Bracket math — accounts for CLI rounding (Kalshi settles on integer
    °F rounding of the 2-min ASOS average, so the YES window shifts ±0.5°F
    in true-temperature space):
      - B-bracket (both floor and cap set, both CLI ints inclusive in YES):
        True-temp YES window = [floor − 0.5, cap + 0.5)  (2°F wide)
        P(YES) = Φ((cap + 0.5 − μ)/σ) − Φ((floor − 0.5 − μ)/σ)
      - T-bracket warm tail (only floor set, YES if CLI > floor):
        True-temp YES = T ≥ floor + 0.5
        P(YES) = 1 − Φ((floor + 0.5 − μ)/σ)
      - T-bracket cold tail (only cap set, YES if CLI < cap):
        True-temp YES = T < cap − 0.5
        P(YES) = Φ((cap − 0.5 − μ)/σ)

      - edge_BUY_YES = P(YES) − yes_ask/100
      - edge_BUY_NO  = (1 − P(YES)) − no_ask/100
    """
    floor = packet.get("floor")
    cap = packet.get("cap")
    yes_ask_c = packet.get("yes_ask_c")
    no_ask_c = packet.get("no_ask_c")

    # μ — match LLM prompt Step 4: bias-corrected lowest-MAE source.
    # The wethr per-model accuracy (`model_mae_recent.per_model.{src}.mae`)
    # is the authoritative ranking per station/direction. Using single-best
    # beats median: May 14 settled (n=16) showed NBM-alone 68.8% directional
    # accuracy vs median 56.2% — picking the best source is materially better
    # than blending with worse ones.
    # Fallback when MAE data is unavailable (e.g., wethr 429): median of
    # available raw forecasts (the old default — safe neutral aggregation).
    source_keys = [("NBP", "mu_nbp"), ("NBM", "mu_nbm"),
                    ("HRRR", "mu_hrrr"), ("ECMWF-IFS", "mu_ecmwf")]
    per_model = (packet.get("model_mae_recent") or {}).get("per_model") or {}
    ranked: list[tuple[float, float, str]] = []   # (mae, bias_corrected_mu, src)
    raw_mus: list[float] = []
    for src_name, mu_key in source_keys:
        raw = packet.get(mu_key)
        if raw is None:
            continue
        try:
            raw_f = float(raw)
        except (TypeError, ValueError):
            continue
        raw_mus.append(raw_f)
        info = per_model.get(src_name) or {}
        mae = info.get("mae")
        if mae is None:
            continue
        try:
            bias = float(info.get("bias") or 0.0)
            ranked.append((float(mae), raw_f - bias, src_name))
        except (TypeError, ValueError):
            continue
    # 2026-05-16: consensus-outlier guard. Picking the single-best-MAE
    # source can anchor μ to an outlier (KMSP-26MAY16-B80.5 case:
    # NBM=81.6, HRRR=78.8, ECMWF=78.0 → best-MAE NBM picked, but it was
    # 2.8°F above the raw median 78.8°F). When best-MAE μ differs from
    # the raw forecast median by > MU_OUTLIER_THRESHOLD_F, fall back to
    # the bias-corrected median (or raw median when bias data unavailable).
    # Threshold 1.5°F: empirically captures source disagreement worth
    # distrusting while letting normal MAE-driven picks through.
    MU_OUTLIER_THRESHOLD_F = 1.5

    def _median(vals):
        if not vals: return None
        vs = sorted(vals)
        n = len(vs)
        return (vs[n//2] + vs[~(n//2)]) / 2.0

    mu_method = "none"
    mu_best_mae = None
    mu_consensus_raw = _median(raw_mus)
    # Bias-corrected median: apply each source's bias before taking median
    # (matches the spirit of bias-correction without trusting any single
    # source's MAE rank).
    if ranked:
        # ranked is [(mae, bias_corrected_mu, src), ...]
        mu_consensus_corr = _median([row[1] for row in ranked])
    else:
        mu_consensus_corr = None

    if ranked:
        ranked.sort(key=lambda x: x[0])  # lowest MAE first
        mu_best_mae = ranked[0][1]
        if (mu_consensus_corr is not None
                and abs(mu_best_mae - mu_consensus_corr) > MU_OUTLIER_THRESHOLD_F):
            mu = mu_consensus_corr
            mu_method = f"consensus_median (best-MAE {ranked[0][2]} was outlier {mu_best_mae - mu_consensus_corr:+.2f}°F)"
        else:
            mu = mu_best_mae
            mu_method = f"best_mae_{ranked[0][2]}"
    elif raw_mus:
        # No MAE data — fall back to median of raw forecasts.
        mu = mu_consensus_raw
        mu_method = "raw_median (no MAE data)"
    else:
        return None

    # 2026-05-16 (EVENING): nn_match — k-NN heating/cooling curve matcher
    # against /home/ubuntu/data/heating_traces.sqlite (5-min resolution, 17
    # weather vars, 20 stations × 16 years). Primary μ when packet has enough
    # trajectory data (>= 3 hourly obs). Backtest:
    #   - paper_min_bot settled n=41: nn 1.58°F MAE vs bot_mu 1.96°F (19% better)
    #   - LOW 20-station 36k cells: nn 1.50°F vs pace 3.51°F (57% better)
    #   - HIGH 20-station: ~13% better than pace baseline
    # Falls back to anchored/rm_ceiling/consensus when nn_match returns None.
    series = packet.get("series") or ""
    is_high = "HIGH" in series
    is_low = series.startswith("KXLOW")
    rm_for_mu = packet.get("running_min_or_max")
    pb = packet.get("pace_band") if is_high else packet.get("pace_low_band")
    pmed_for_mu: Optional[float] = None
    if pb and isinstance(pb, dict):
        try:
            _pm = float(pb.get("median") or 0)
            if 0.7 <= _pm <= 1.0:
                pmed_for_mu = _pm
        except (TypeError, ValueError):
            pmed_for_mu = None

    _nn_sigma_proj: Optional[float] = None
    try:
        from nn_shadow import shadow_nn_proj as _shadow_nn_proj
        _nn_res = _shadow_nn_proj(packet)
    except Exception as e:
        log.debug("nn_match unavailable: %s", e)
        _nn_res = None
    if _nn_res and _nn_res.get("mu") is not None:
        try:
            mu = float(_nn_res["mu"])
            _side = _nn_res.get("side", "")
            _n = _nn_res.get("n_neighbors")
            mu_method = (f"nn_match_{_side}_n{_n}"
                         + ("_locked" if _nn_res.get("extreme_locked") else ""))
            if _nn_res.get("sigma") is not None:
                _nn_sigma_proj = float(_nn_res["sigma"])
            # 2026-05-17: stash full nn_match metadata for judgment.py NN_MATCH
            # block. Without this the LLM only sees mu_method/mu_chosen/
            # sigma_chosen — but the prompt examples reference n_neighbors,
            # pool_size, locked status, sigma_raw, bias_correction, top
            # analog days. Stashing here lets judgment.py render the rich
            # block while keeping the metadata logged in candidates.jsonl
            # for backtest attribution.
            # 2026-05-18: compute bracket-fraction from full analog distribution
            # so the LLM packet shows in% / above% / below% of all 50 neighbors
            # (replaces top-3 cherry-pick view that caused PHL B96.5 loss).
            _asum = _nn_res.get("analog_summary") or {}
            _extremes = _asum.get("day_extremes") or []
            _floor_v = packet.get("floor")
            _cap_v = packet.get("cap")
            _bk_kind = (packet.get("bracket_kind") or "B").upper()
            _in_pct = _abv_pct = _bel_pct = None
            if _extremes:
                _n_ex = len(_extremes)
                if _bk_kind == "B" and _floor_v is not None and _cap_v is not None:
                    _yes_lo = float(_floor_v) - 0.5
                    _yes_hi = float(_cap_v) + 0.5
                    _in_c = sum(1 for x in _extremes if _yes_lo <= x < _yes_hi)
                    _abv_c = sum(1 for x in _extremes if x >= _yes_hi)
                    _bel_c = sum(1 for x in _extremes if x < _yes_lo)
                    _in_pct = round(100.0 * _in_c / _n_ex, 1)
                    _abv_pct = round(100.0 * _abv_c / _n_ex, 1)
                    _bel_pct = round(100.0 * _bel_c / _n_ex, 1)
                elif _bk_kind == "T" and _floor_v is not None and _cap_v is None:
                    _yes_lo = float(_floor_v) + 0.5
                    _in_c = sum(1 for x in _extremes if x >= _yes_lo)
                    _bel_c = _n_ex - _in_c
                    _in_pct = round(100.0 * _in_c / _n_ex, 1)
                    _bel_pct = round(100.0 * _bel_c / _n_ex, 1)
                elif _bk_kind == "T" and _cap_v is not None and _floor_v is None:
                    _yes_hi = float(_cap_v) - 0.5
                    _in_c = sum(1 for x in _extremes if x < _yes_hi)
                    _abv_c = _n_ex - _in_c
                    _in_pct = round(100.0 * _in_c / _n_ex, 1)
                    _abv_pct = round(100.0 * _abv_c / _n_ex, 1)

            packet["_nn_match_meta"] = {
                "side": _side,
                "n_neighbors": _n,
                "pool_size": _nn_res.get("pool_size"),
                "extreme_locked": _nn_res.get("extreme_locked", False),
                "sigma_raw": _nn_res.get("sigma_raw"),
                "sigma_factor": _nn_res.get("sigma_factor"),
                "bias_correction": _nn_res.get("bias_correction"),
                "fit_quality_thresh": _nn_res.get("fit_quality_thresh"),
                "median_delta": _nn_res.get("median_delta"),
                "cur_tmpf": _nn_res.get("cur_tmpf"),
                "top_neighbors": (_nn_res.get("neighbors") or [])[:3],
                "analog_summary": _asum,
                "analog_in_bracket_pct": _in_pct,
                "analog_above_pct": _abv_pct,
                "analog_below_pct": _bel_pct,
            }
        except (TypeError, ValueError):
            _nn_res = None  # fall through

    # 2026-05-16 (ACTION D): Anchored μ — obs-derived primary anchor.
    # Backtest (V2 max n=394, 6 days): anchored 77.9% acc / MAE 1.12 / bias
    # -0.01 vs v2_blended 73.1% / 1.14 / -0.57. The anchored formula uses
    # forecast consensus as a prior and shifts it by today's obs vs the
    # expected pace-based trajectory:
    #   μ_anchored = consensus_corr + (rm − consensus_corr × pace_median)
    # Only valid when pace_median ∈ [0.7, 1.0] (mid-afternoon for HIGH,
    # late-cooling for LOW). Below 0.7, pace projection diverges (small
    # denominator amplifies noise). HIGH-only for now — LOW pace semantics
    # differ and need separate validation (backlog).
    #
    # PHYSICAL FLOOR: peak ≥ current rm for HIGH (can't drop). Caps anchored
    # at rm when bias-corrections push consensus below rm. KMSP 2026-05-16
    # case had consensus 77.56 with rm 78.0 → without floor, anchored
    # would be 77.5; with floor it's 78.0 (rm itself = no-further-climb
    # estimate).
    # Only run anchored/rm_ceiling when nn_match did not produce a μ.
    if (not (_nn_res and _nn_res.get("mu") is not None) and is_high
            and rm_for_mu is not None and pmed_for_mu is not None
            and mu_consensus_corr is not None):
        try:
            mu_anchored = mu_consensus_corr + (float(rm_for_mu) - mu_consensus_corr * pmed_for_mu)
            # Physical floor: HIGH peak ≥ rm.
            if float(rm_for_mu) > mu_anchored:
                mu_anchored = float(rm_for_mu)
                anchor_floored = True
            else:
                anchor_floored = False
            mu = mu_anchored
            mu_method = (f"anchored (pace_med={pmed_for_mu:.3f}, rm={rm_for_mu:.1f}"
                         + (", floored_at_rm" if anchor_floored else "") + ")")
        except (TypeError, ValueError):
            pass  # keep existing mu

    # 2026-05-16: LOW-side anchored via rm-ceiling. Backtest on paper_min_bot
    # n=26 settled positions: MAE 0.73°F vs old_mu 1.95°F (2.7x improvement),
    # p95 2.0°F vs 4.1°F. Logic: by the time LOW d+0 is evaluated (pre-dawn
    # 1.5-5.5 local OR late-evening 22-24 local), cooling is mostly complete
    # — rm ≈ day_min already. So cap consensus at rm (day_min can't be ABOVE
    # what's been observed today). When rm ≥ consensus, keep consensus (more
    # cooling expected).
    #
    # Gated on pace_low_median ≥ 0.7 to avoid early-evening cases where rm
    # hasn't yet approached day_min. KMSP-style metar stations + standard
    # ASOS both well-behaved in this regime.
    # Skip when nn_match already produced a μ.
    elif (not (_nn_res and _nn_res.get("mu") is not None) and is_low
            and rm_for_mu is not None and pmed_for_mu is not None
            and mu_consensus_corr is not None):
        try:
            cons_v = float(mu_consensus_corr)
            rm_v = float(rm_for_mu)
            if cons_v > rm_v:
                mu = rm_v
                mu_method = (f"low_rm_ceiling (pace_low_med={pmed_for_mu:.3f}, "
                             f"consensus {cons_v:.1f} > rm {rm_v:.1f})")
            # else: consensus ≤ rm — more cooling expected, keep consensus
        except (TypeError, ValueError):
            pass

    # σ — match LLM prompt Step 4: σ = 1.25 × MAE.
    # 1.25 converts MAE → Gaussian stddev (MAE = σ·√(2/π) ≈ 0.798·σ, so
    # σ ≈ 1.253·MAE for a normal error distribution). Using raw MAE
    # understates the tail and inflates P(direction).
    # Source hierarchy (matches what Claude does in Step 4):
    #   1. lowest per-model MAE (matches "best-MAE source" rule)
    #   2. ensemble MAE
    #   3. NBP's own σ (already a stddev, no 1.25 conversion needed)
    #   4. default_sigma constant
    # Bound to [1.0°F, 4.0°F]: floor prevents pathological tightness on
    # stable regimes; ceiling guards against a noisy MAE outlier
    # collapsing P(YES) toward coin-flip.
    mae_info = packet.get("model_mae_recent") or {}
    per_model = mae_info.get("per_model") or {}
    maes = [
        info["mae"] for info in per_model.values()
        if isinstance(info, dict) and info.get("mae") is not None
    ]
    ensemble_mae = (mae_info.get("ensemble") or {}).get("mae")
    nbp_sigma = packet.get("nbp_sigma")
    if maes:
        sigma_raw = float(min(maes)) * 1.25
    elif ensemble_mae is not None:
        sigma_raw = float(ensemble_mae) * 1.25
    elif nbp_sigma is not None:
        sigma_raw = float(nbp_sigma)  # already a stddev, not MAE
    else:
        sigma_raw = default_sigma
    try:
        sigma = max(1.0, min(4.0, float(sigma_raw)))
    except (TypeError, ValueError):
        sigma = max(1.0, min(4.0, float(default_sigma)))

    # d-0 near-peak σ inflation: when rm has already exceeded forecast μ and
    # the daily extreme is within 2h, true intraday variance is wider than the
    # historical MAE captures (MAE = d-1 forecast residual, not d-0 with the
    # obs-vs-forecast gap already widening). Floor σ at 2.5°F so P(YES)
    # estimates near a bracket aren't artificially tightened. (PHX 2026-05-15
    # B99.5: σ=1.46°F → P(NO)=99% when realistic σ=2.5°F → P(NO)=92%.)
    rm = packet.get("running_min_or_max")
    clock = packet.get("local_clock") or {}
    local_hour = clock.get("local_hour")
    extreme_hour = (clock.get("peak_hour_local")
                    if packet.get("series") == "KXHIGH"
                    else clock.get("min_hour_local"))
    series = packet.get("series") or ""
    if (rm is not None and local_hour is not None and extreme_hour is not None
        and (extreme_hour - 2.0) <= local_hour <= (extreme_hour + 0.5)):
        rm_above_mu = (series == "KXHIGH" and rm > mu) or (series == "KXLOW" and rm < mu)
        if rm_above_mu:
            sigma = max(sigma, 2.5)

    # 2026-05-16: σ floor from today's intraday variance. Historical MAE
    # is a 14-day window — it averages stable days with volatile ones. On a
    # volatile-today day, that's an underestimate. temp_history_range_60m
    # captures today's actual hour-over-hour variance; half-range is a rough
    # 1σ proxy (assumes near-uniform distribution over the range). Floor σ
    # at that value so confidence reflects today's regime, not the 14-day
    # average. KMSP 2026-05-16: 60m range 3.6°F → σ floor 1.8°F vs MAE-derived
    # 1.06°F. Conservative side; never reduces σ.
    th_rng = packet.get("temp_history_range_60m") or {}
    intraday_range = th_rng.get("range_f")
    if intraday_range is not None and intraday_range > 0:
        sigma = max(sigma, float(intraday_range) / 2.0)

    # 2026-05-16 (ACTION F): σ floor on forecast source disagreement. When
    # sources disagree by > 3°F (disagreement_f = max pairwise diff), the
    # forecast layer itself is uncertain — confidence shouldn't be tight.
    # half-disag is a coarse 1σ proxy. KMSP 2026-05-16 had disag=3.6 →
    # σ floor 1.8°F. Compounds with intraday range and near-peak floors
    # (always max, never min). Final sigma still capped at 4.0.
    disag_f = packet.get("disagreement_f")
    if disag_f is not None:
        try:
            disag_v = float(disag_f)
            if disag_v > 0:
                sigma = max(sigma, disag_v / 2.0)
        except (TypeError, ValueError):
            pass

    # 2026-05-16 (evening): σ floor from nn_match neighbor stdev. When the
    # k-NN matcher returns its mu_proj, sigma_proj_f = stdev of neighbor
    # day-extreme residuals, i.e. how much projection varies across matched
    # historical days. Use as floor (never reduces).
    if _nn_sigma_proj is not None and _nn_sigma_proj > 0:
        sigma = max(sigma, _nn_sigma_proj)

    # Re-clamp to ceiling after all floors applied
    sigma = min(4.0, sigma)

    # P(YES) per bracket shape, with CLI rounding applied to all boundaries.
    # 2026-05-18: TRUNCATED distribution given physical rm constraint.
    # HIGH series: day_high ≥ rm. LOW series: day_low ≤ rm. The old
    # untruncated math assigned probability mass to physically-impossible
    # outcomes (e.g., day_high < rm when rm has already been observed),
    # producing inflated edges on rm-in-YES cases like KXHIGHTMIN-B66.5
    # (logged 67.8pp edge that was really ~26pp after truncation). Uses
    # the same _p_yes_constrained from nn_shadow_strategy — single source
    # of truth.
    from nn_shadow_strategy import _p_yes_constrained
    is_high_series = (packet.get("series") or "").startswith("KXHIGH") or "HIGH" in (packet.get("series") or "")
    days_out = packet.get("days_out")
    rm_for_trunc = None
    if days_out == 0:
        try:
            _rm_raw = packet.get("running_min_or_max")
            if _rm_raw is not None:
                rm_for_trunc = float(_rm_raw)
        except (TypeError, ValueError):
            rm_for_trunc = None
    if floor is None and cap is None:
        return None
    p_yes = _p_yes_constrained(mu, sigma, floor, cap, rm_for_trunc, is_high_series)
    p_yes = max(0.0, min(1.0, p_yes))
    p_no = 1.0 - p_yes

    edges = []
    if yes_ask_c is not None:
        edges.append(("BUY_YES", p_yes - yes_ask_c / 100.0, p_yes))
    if no_ask_c is not None:
        edges.append(("BUY_NO", p_no - no_ask_c / 100.0, p_no))
    if not edges:
        return None
    side, best_edge, prob = max(edges, key=lambda x: x[1])
    return {
        "side": side, "edge": best_edge, "prob": prob,
        "mu": mu, "sigma": sigma,
        # 2026-05-16: instrumentation for the consensus-outlier guard +
        # intraday-σ floor so backtest can attribute outcomes to which knob.
        "mu_method": mu_method,
        "mu_best_mae": mu_best_mae,
        "mu_consensus_raw": mu_consensus_raw,
        "mu_consensus_corr": mu_consensus_corr,
        "sigma_intraday_floor_used": (
            intraday_range / 2.0 if intraday_range is not None and intraday_range > 0
            else None
        ),
    }


