def run_entry_loop(rt: Runtime) -> None:
    if rt.ctx.kill_switch_active:
        return
    _entry_loop_t0 = time.time()
    # Insufficient-funds gate: if wallet can't afford a buy at typical
    # prices, skip the entire cycle (no prefetch, no LLM). Bot resumes
    # automatically when balance climbs back above min_cycle_balance_usd.
    min_bal = config.GUARDRAILS.get("min_cycle_balance_usd", 0.0)
    if min_bal > 0:
        bal = kalshi_client.get_balance_cached(force_refresh=True)
        if bal is not None and bal < min_bal:
            log.info("entry cycle SKIPPED: balance $%.2f < $%.2f", bal, min_bal)
            rt.hourly_cycles_skipped_funds += 1
            discord_send(
                f"⏸ cycle {rt.cycles} entry SKIPPED — wallet $%.2f < $%.2f "
                f"(insufficient funds)" % (bal, min_bal)
            )
            return
    # 2026-05-16 (B3 fix): wallet_held_tickers already populated by
    # reconcile_positions_with_kalshi at the top of one_cycle (a few seconds
    # earlier). The prior `rt.refresh_wallet_positions()` here issued a
    # SECOND /portfolio/positions REST call per cycle on the same data,
    # wasting one round-trip and opening a ~ms race window where the two
    # snapshots could disagree. The 5-10s freshness lag from reconcile is
    # well within the sibling-bot order cadence (cycles are ~15min apart).
    # Reset per-cycle counters used by quality + correlation gates.
    # Seed the (station, side, climate_day) counter from the Kalshi-
    # authoritative wallet ticker set, so the cap blocks ANY new entry
    # on a station-side that already has a live position — including
    # positions opened by OTHER bots on this wallet (V2 max, V2 min),
    # which are in wallet_held_tickers but may NOT be in rt.positions.
    rt.cycle_buys_count = 0
    rt.cycle_buys_by_station_side = {}
    for _tk in rt.wallet_held_tickers:
        _parsed = market_universe.parse_ticker(_tk)
        if _parsed is None:
            continue
        _side = "HIGH" if _parsed.series_prefix == "KXHIGH" else "LOW"
        _key = (_parsed.station, _side, _parsed.climate_day)
        rt.cycle_buys_by_station_side[_key] = (
            rt.cycle_buys_by_station_side.get(_key, 0) + 1
        )
    # Prefetch ALL data Claude needs this cycle, ONCE, BEFORE prescreen.
    # build_entry_packet + prescreen read from rt.cycle_data.
    rt.cycle_data = live_data.prefetch(list(config.STATIONS))
    candidates = market_universe.list_candidates()
    log.info(
        "entry loop: %d candidates, wallet has %d open tickers",
        len(candidates), len(rt.wallet_held_tickers),
    )
    if USE_KALSHI_WS and candidates:
        try:
            kalshi_ws.subscribe([c.ticker for c in candidates])
        except Exception as _ws_se:
            log.warning("kalshi_ws.subscribe failed: %s — execute_buy will use REST fallback", _ws_se)

    # Code-only top-up pass: completes partial-filled positions toward
    # min_buy_usd without burning Claude tokens. Runs BEFORE the normal
    # candidate loop; afterwards top-up tickers fall back to "already_held"
    # in Gate A.
    try:
        n_topups = _process_topups(rt, candidates)
        if n_topups > 0:
            log.info("top-up pass: attempted %d", n_topups)
    except Exception:
        log.exception("top-up pass error")

    # Bucket reasons for end-of-loop summary.
    from collections import Counter
    rejection_counts: Counter = Counter()
    survivors: list = []

    for cand in candidates:
        if rt.shutdown:
            return

        cand_rec = {
            "ts": time.time(),
            "cycle": rt.cycles,
            "ticker": cand.ticker,
            "station": cand.station,
            "city": cand.city_code,
            "series": cand.series_prefix,
            "bracket_kind": cand.bracket_kind,
            "floor": cand.floor,
            "cap": cand.cap,
            "climate_day": cand.climate_day,
            "verdict": None,
            "verdict_reason": None,
        }

        if cand.ticker in rt.wallet_held_tickers or cand.ticker in rt.positions:
            rejection_counts["already_held"] += 1
            cand_rec["verdict"] = "skip_already_held"
            state.log_candidate(cand_rec)
            continue

        # 2026-05-16 (C3 fix): early-skip on already-settled tickers BEFORE the
        # ~50ms packet build. Prescreen's max_settled_threshold_cents=90 check
        # was already firing for these (~170/cycle = ~10K/day in today's data)
        # but only AFTER a full build_entry_packet + live_data lookups. Reading
        # the dominant ask from the raw market record short-circuits the
        # wasted work while preserving the candidate log row (with verdict +
        # reason). Same threshold + reason string as the prescreen check that
        # would otherwise fire downstream.
        _ya_d = cand.market.get("yes_ask_dollars")
        _na_d = cand.market.get("no_ask_dollars")
        try:
            _ya_c_early = int(round(float(_ya_d) * 100)) if _ya_d is not None else 0
        except (TypeError, ValueError):
            _ya_c_early = 0
        try:
            _na_c_early = int(round(float(_na_d) * 100)) if _na_d is not None else 0
        except (TypeError, ValueError):
            _na_c_early = 0
        _max_ask_early = max(_ya_c_early, _na_c_early)
        _settled_thresh = config.PRESCREEN.get("max_settled_threshold_cents", 99)
        if _max_ask_early >= _settled_thresh:
            rejection_counts["prescreen: already settled: max"] += 1
            cand_rec["verdict"] = "skip_prescreen"
            cand_rec["verdict_reason"] = (
                f"already settled: max ask {_max_ask_early}c >= {_settled_thresh}c"
            )
            state.log_candidate(cand_rec)
            continue

        packet = build_entry_packet(rt, cand)
        if packet is None:
            rejection_counts["no_packet (missing bid/ask)"] += 1
            cand_rec["verdict"] = "skip_no_packet"
            state.log_candidate(cand_rec)
            continue

        rej = prescreen(packet)

        # Snapshot packet AFTER prescreen so _edge_info (set by prescreen)
        # is captured in the analytics record.
        cand_rec["packet"] = _packet_log_summary(packet)

        if rej:
            key = " ".join(rej.split()[:3])
            rejection_counts[f"prescreen: {key}"] += 1
            cand_rec["verdict"] = "skip_prescreen"
            cand_rec["verdict_reason"] = rej
            state.log_candidate(cand_rec)
            continue

        _side = "HIGH" if cand.series_prefix == "KXHIGH" else "LOW"
        _key = (cand.station, _side, cand.climate_day)
        _cap = config.GUARDRAILS.get("max_buys_per_station_side", 999)
        if rt.cycle_buys_by_station_side.get(_key, 0) >= _cap:
            rejection_counts[f"correlation_cap: {cand.station} {_side}"] += 1
            cand_rec["verdict"] = "skip_correlation_cap"
            cand_rec["verdict_reason"] = f"existing {rt.cycle_buys_by_station_side.get(_key, 0)} >= cap {_cap}"
            state.log_candidate(cand_rec)
            continue

        # Re-dispatch cool-down: if we LLM-dispatched this ticker recently
        # AND nothing material changed (price ≥5c move OR rm crossed a bracket
        # boundary), skip. Prevents the flip-flop on noisy 30-min trend signals
        # that drove the PHX 2026-05-15 BUY (3 dispatches in 40 min, conviction
        # climbed 0.85→0.93 solely on trend reversal).
        last = rt.last_dispatch_state.get(cand.ticker)
        cd_sec = config.PRESCREEN.get("dispatch_cooldown_sec", 1800)
        if last is not None and (rt.ctx.now_utc - last["ts"]) < cd_sec:
            elapsed = rt.ctx.now_utc - last["ts"]
            cd_price = config.PRESCREEN.get("dispatch_cooldown_price_delta_c", 5)
            last_no = last.get("no_ask_c") or 50
            cur_no = packet.get("no_ask_c") or 50
            price_delta = abs(cur_no - last_no)
            last_rm_val = last.get("rm")
            cur_rm_val = packet.get("running_min_or_max")
            rm_crossed = False
            if last_rm_val is not None and cur_rm_val is not None:
                for b in (cand.floor, cand.cap):
                    if b is None:
                        continue
                    if (last_rm_val < b) != (cur_rm_val < b):
                        rm_crossed = True
                        break
            if price_delta < cd_price and not rm_crossed:
                rejection_counts[f"cooldown ({elapsed:.0f}s, Δ{price_delta}c)"] += 1
                cand_rec["verdict"] = "skip_cooldown"
                cand_rec["verdict_reason"] = (
                    f"dispatched {elapsed:.0f}s ago, price Δ {price_delta}c "
                    f"< {cd_price}c, no rm cross"
                )
                state.log_candidate(cand_rec)
                continue

        # F-OBS-ANCHORED-PRE 2026-05-17: structural enforcement of the
        # prompt's Step 7.5 hard veto. The prompt says BUY_NO on a B-bracket
        # must be SKIPPED when wethr_temp_f is inside the YES window AND
        # h_to_extreme > 0.5h, unless wethr's running extreme has already
        # escaped (a) OR the probable band predicts escape + the 60m
        # regression slope+r²+range agree (b). Empirically the LLM was
        # bypassing this veto in heat-outlier reads (HOU-B88.5 -$19.61,
        # OKC-B87.5 -$16.80, both settled 2026-05-16). Moving the check to
        # prescreen so the LLM can't override it. Backtest 2026-05-15..17
        # n=22 BUY_NO B-bracket settlements: blocks 2 losers (saves
        # $36.41), zero false positives on winners. Defense in depth — the
        # LLM-side Step 7.5 text stays.
        # 2026-05-17 21:xx UTC: gated by config.OBS_ANCHORED_PRE_ENABLED
        # (default False per Chris).
        if (getattr(config, "OBS_ANCHORED_PRE_ENABLED", False)
                and cand.bracket_kind == "B" and cand.floor is not None
                and cand.cap is not None):
            _edge_info = packet.get("_edge_info") or {}
            if _edge_info.get("side") == "BUY_NO":
                _wo = packet.get("wethr_obs") or {}
                _wt = _wo.get("temp_f")
                _floor_f = float(cand.floor)
                _cap_f = float(cand.cap)
                _yes_lo = _floor_f - 0.5
                _yes_hi = _cap_f + 0.5
                if _wt is not None and _yes_lo <= _wt < _yes_hi:
                    # In YES window — check time-to-extreme
                    _clk = packet.get("local_clock") or {}
                    _lh = _clk.get("local_hour")
                    _is_high = cand.series_prefix == "KXHIGH"
                    _ex_hr = (_clk.get("peak_hour_local") if _is_high
                              else _clk.get("min_hour_local"))
                    _h_to_ex = None
                    if _lh is not None and _ex_hr is not None:
                        _h_to_ex = _ex_hr - _lh
                        if _h_to_ex < 0:
                            _h_to_ex += 24
                    if _h_to_ex is not None and _h_to_ex > 0.5:
                        # Gate engaged — check bypass (a) and (b).
                        _bypass_a = False
                        _prob_escape = False
                        if _is_high:
                            _wh = _wo.get("high_f")
                            if _wh is not None and _wh > _yes_hi:
                                _bypass_a = True
                            _whp = _wo.get("highest_probable_f")
                            _prob_escape = _whp is not None and _whp > _yes_hi
                        else:
                            _wl = _wo.get("low_f")
                            if _wl is not None and _wl < _yes_lo:
                                _bypass_a = True
                            _wlp = _wo.get("lowest_probable_f")
                            _prob_escape = _wlp is not None and _wlp < _yes_lo
                        _bypass_b = False
                        if _prob_escape:
                            _reg = packet.get("obs_trend_60m_regression") or {}
                            _slp = _reg.get("slope_f_per_h")
                            _r2 = _reg.get("r_squared")
                            _rng = (packet.get("temp_history_range_60m")
                                    or {}).get("range_f")
                            _slp_ok = _slp is not None and (
                                (_is_high and _slp > 0)
                                or (not _is_high and _slp < 0)
                            )
                            _r2_ok = _r2 is not None and _r2 >= 0.5
                            _rng_ok = _rng is not None and _rng >= 2.0
                            _bypass_b = _slp_ok and _r2_ok and _rng_ok
                        if not (_bypass_a or _bypass_b):
                            # Block.
                            _wext = (_wo.get("high_f") if _is_high
                                     else _wo.get("low_f"))
                            _wprob = (_wo.get("highest_probable_f") if _is_high
                                      else _wo.get("lowest_probable_f"))
                            _msg = (
                                f"obs_anchored_pre: wethr_temp_f={_wt}°F in YES "
                                f"window [{_yes_lo:.1f}, {_yes_hi:.1f}), "
                                f"h_to_extreme={_h_to_ex:.2f}h, "
                                f"wethr_{'high' if _is_high else 'low'}={_wext}, "
                                f"wethr_{'highest' if _is_high else 'lowest'}_probable={_wprob} "
                                f"(no bypass)"
                            )
                            rejection_counts["obs_anchored_pre"] += 1
                            cand_rec["verdict"] = "skip_prescreen"
                            cand_rec["verdict_reason"] = _msg
                            state.log_candidate(cand_rec)
                            discord_send(
                                f"🛑 **F-OBS-ANCHORED-PRE** SKIP `{cand.ticker}` — "
                                f"wethr_temp={_wt}°F INSIDE YES window "
                                f"[{_yes_lo:.1f}, {_yes_hi:.1f}), "
                                f"{_h_to_ex:.1f}h to {'peak' if _is_high else 'min'}, "
                                f"no escape evidence "
                                f"(wethr_{'high' if _is_high else 'low'}={_wext}, "
                                f"probable={_wprob}). "
                                f"Saved LLM call; would have likely lost."
                            )
                            continue

        # Pass — will be dispatched to LLM. Verdict set later when LLM returns.
        cand_rec["verdict"] = "passed_prescreen"
        survivors.append((cand, packet, cand_rec))

    # De-duplicate survivors: keep only the highest-numerical-edge candidate
    # per (station, side, climate_day). Multiple brackets on the same
    # (city, side) for the same date are correlated — the bot already
    # caps to 1 position via max_buys_per_station_side, so dispatching all
    # of them to the LLM and rejecting all-but-one post-LLM is wasteful.
    # We pick the best-edge bracket pre-LLM and shadow-log the others.
    best_per_key: dict = {}
    dropped_recs = []
    for cand, packet, cand_rec in survivors:
        _side = "HIGH" if cand.series_prefix == "KXHIGH" else "LOW"
        _key = (cand.station, _side, cand.climate_day)
        _edge = (packet.get("_edge_info") or {}).get("edge")
        if _edge is None:
            _edge = -999.0
        prev = best_per_key.get(_key)
        if prev is None or _edge > prev[3]:
            if prev is not None:
                _, _, prev_rec, _ = prev
                prev_rec["verdict"] = "skip_dedupe_lower_edge"
                dropped_recs.append(prev_rec)
            best_per_key[_key] = (cand, packet, cand_rec, _edge)
        else:
            cand_rec["verdict"] = "skip_dedupe_lower_edge"
            dropped_recs.append(cand_rec)
    deduped = [(c, p, r) for (c, p, r, _e) in best_per_key.values()]
    for r in dropped_recs:
        state.log_candidate(r)
    n_dropped = len(survivors) - len(deduped)
    if n_dropped > 0:
        log.info("entry loop dedupe: dropped %d sibling-bracket candidates "
                 "(kept best-edge per (station, side, date))", n_dropped)
        rejection_counts["dedupe: sibling bracket lower-edge"] = n_dropped
    survivors = deduped

    # 2026-05-19: LLM dispatch gating for push pure-code architecture.
    # config.LLM_DISPATCH_MODE values:
    #   "off"      — push worker handles all BUYs; LLM never dispatched
    #   "yes_only" — push handles BUY_NO; LLM dispatched only on BUY_YES
    #   "all"      — legacy behavior (every survivor → LLM)
    _llm_mode = getattr(config, "LLM_DISPATCH_MODE", "all")
    if _llm_mode in ("off", "yes_only"):
        kept = []
        for (cand, packet, cand_rec) in survivors:
            ei = packet.get("_edge_info") or {}
            best_side = ei.get("side") or ""
            if _llm_mode == "off":
                # Skip ALL LLM dispatches — push owns the trading path.
                cand_rec["verdict"] = "skip_llm_dispatch_off"
                cand_rec["verdict_reason"] = "LLM_DISPATCH_MODE=off (push owns trades)"
                state.log_candidate(cand_rec)
                rejection_counts["llm_dispatch: mode=off"] += 1
            elif _llm_mode == "yes_only":
                if best_side == "BUY_NO":
                    cand_rec["verdict"] = "skip_llm_dispatch_yes_only"
                    cand_rec["verdict_reason"] = "LLM_DISPATCH_MODE=yes_only (push owns BUY_NO)"
                    state.log_candidate(cand_rec)
                    rejection_counts["llm_dispatch: mode=yes_only BUY_NO"] += 1
                else:
                    kept.append((cand, packet, cand_rec))
        if _llm_mode == "off":
            survivors = []
        else:  # yes_only
            survivors = kept
        log.info("LLM dispatch gate: mode=%s, %d survivors after gate",
                 _llm_mode, len(survivors))

    log.info(
        "entry loop pre-screen results: %d survivors out of %d candidates",
        len(survivors), len(candidates),
    )
    # Roll cycle rejects into the hourly window for the summary.
    rt.hourly_reject_counts.update(rejection_counts)
    # Per-cycle summary row for date-partitioned analysis.
    try:
        cycle_rec = {
            "ts": time.time(),
            "cycle": rt.cycles,
            "n_candidates": len(candidates),
            "n_survivors": len(survivors),
            "n_llm_dispatched": len(survivors),
            "rejection_counts": dict(rejection_counts),
            "balance_usd": kalshi_client.get_balance_cached(),
            "open_positions": rt.ctx.open_positions_count,
        }
        state.log_cycle(cycle_rec)
    except Exception:
        log.exception("cycle summary log failed")
    for reason, n in rejection_counts.most_common():
        log.info("  rejected %4d × %s", n, reason)
    # Discord: surface entry funnel per cycle. 0-dispatch cycles get a
    # distinctive emoji + full top-5 rejects so it's clear at a glance why
    # nothing went through. Non-zero gets the regular dispatched-N format.
    _entry_elapsed = time.time() - _entry_loop_t0
    n_dispatch = len(survivors)
    if n_dispatch == 0:
        # Nothing dispatched — show full funnel breakdown for context.
        top_rejects = ", ".join(
            f"{n}×{r.replace('prescreen: ', '')[:30]}"
            for r, n in rejection_counts.most_common(5)
        ) or "(no candidates)"
        discord_send(
            f"😴 **cycle {rt.cycles} — 0 DISPATCHED** "
            f"({len(candidates)} candidates, {_entry_elapsed:.0f}s)\n"
            f"Top rejects: {top_rejects}"
        )
    else:
        top_rejects = ", ".join(
            f"{n}×{r.replace('prescreen: ', '')[:25]}"
            for r, n in rejection_counts.most_common(3)
        )
        discord_send(
            f"🔍 cycle {rt.cycles} entry: **{n_dispatch} dispatched** / {len(candidates)} "
            f"({_entry_elapsed:.0f}s). Top rejects: {top_rejects or '(none)'}"
        )

    # Parallel Claude calls. Each subprocess is independent; we cap at
    # ENTRY_PARALLEL_WORKERS to stay rate-limit-safe.
    #
    # Immediate-execute pattern: as_completed yields one future at a time to
    # the main thread, so the buy-execution code runs single-threaded with
    # no lock needed. This minimizes the gap between Claude's decision and
    # the Kalshi order — prices can move 5-30 min, post-process delay = bad.
    from concurrent.futures import ThreadPoolExecutor, as_completed
    workers = max(1, int(config.ENTRY_PARALLEL_WORKERS))
    if not survivors:
        return

    # Circuit-breaker gate: when LLM has failed 3x consecutively, guardrails
    # sets llm_paused_until_ts. Without this gate the entry loop kept dispatching
    # during the 2026-05-15 Anthropic API outage → 565 wasted calls over 3hrs +
    # Discord ping spam. (The pause was only honored in check_buy/check_sell at
    # submit time, which never ran because parse_error short-circuited above it.)
    if rt.ctx.now_utc < rt.ctx.llm_paused_until_ts:
        pause_remaining = rt.ctx.llm_paused_until_ts - rt.ctx.now_utc
        log.warning("entry loop SKIPPING LLM dispatch — circuit breaker active "
                    "for %.0fs more (%d consecutive fails)",
                    pause_remaining, rt.ctx.consecutive_llm_failures)
        for cand, packet, cand_rec in survivors:
            cand_rec["verdict"] = "skip_llm_paused"
            cand_rec["verdict_reason"] = (
                f"llm paused {pause_remaining:.0f}s remaining "
                f"({rt.ctx.consecutive_llm_failures} consecutive fails)"
            )
            state.log_candidate(cand_rec)
        discord_send(
            f"🛑 cycle {rt.cycles} entry **SKIPPED** — LLM circuit-breaker active "
            f"({pause_remaining:.0f}s remaining, {rt.ctx.consecutive_llm_failures} "
            f"consecutive fails). Would have dispatched {len(survivors)} candidates."
        )
        return
    log.info("dispatching %d candidates to %d workers", len(survivors), workers)
    rt.hourly_llm_entry_calls += len(survivors)
    # Record dispatch state for cool-down enforcement on subsequent cycles.
    for cand, packet, _cr in survivors:
        rt.last_dispatch_state[cand.ticker] = {
            "ts": rt.ctx.now_utc,
            "no_ask_c": packet.get("no_ask_c"),
            "rm": packet.get("running_min_or_max"),
        }
    # 2026-05-17: persist to disk so cooldowns survive restarts.
    state.save_dispatch_state(rt.last_dispatch_state)
    g = config.GUARDRAILS
    with ThreadPoolExecutor(max_workers=workers) as ex:
        futs = {
            ex.submit(judgment.judge_entry, packet): (cand, packet, cand_rec)
            for cand, packet, cand_rec in survivors
        }
        for fut in as_completed(futs):
            if rt.shutdown:
                break
            cand, packet, cand_rec = futs[fut]
            try:
                decision = fut.result()
            except Exception as e:
                # NEVER silent — Chris must see every dispatched candidate's outcome.
                log.warning("judge_entry crash on %s: %s", cand.ticker, e)
                discord_send(
                    f"💥 **LLM CRASH** `{cand.ticker}` — "
                    f"{type(e).__name__}: {str(e)[:200]}"
                )
                continue

            # ── POST-LLM CONVICTION CAPS ──────────────────────────────────
            # Three independent caps applied to Claude's stated conviction
            # when structural risk patterns indicate the decision is shakier
            # than the LLM's own number. Each cap is a SOFT veto via
            # min_conviction_for_buy gate downstream — keeps the decision
            # in the record for analysis instead of hard-SKIP-ing.
            _conv_caps_applied = []

            # Cap 1: contradictory key_risk language. If Claude's own listed
            # risks describe the trigger as fragile/temporary/noise, the
            # conviction shouldn't be high. (PHX 2026-05-15 B99.5: risk #2 was
            # "Current dip is wind-induced and temporary; ... aggressive
            # rebound in 2h pre-peak window" — yet conv=0.93.)
            # _RISK_TOKENS defined at module level.
            _risk_text = " ".join(decision.key_risks or []).lower()
            if any(tok in _risk_text for tok in _RISK_TOKENS):
                if decision.conviction > 0.85:
                    _conv_caps_applied.append(
                        f"contradictory_key_risk ({decision.conviction:.2f}→0.85)"
                    )
                    decision.conviction = 0.85

            # NOTE: NWS peak proximity / inside-bracket cap rolled back
            # 2026-05-15 — this was forecast-based filtering of an obs-based
            # bot, contradicting the bot's premise. When NWS gridpoint sits
            # inside the bracket, the bot's whole edge is that obs (rm) can
            # disagree with the forecast central case. Vetoing on the
            # forecast erodes exactly the contrarian stance the bot exists
            # to take.

            # Cap 3: Cheap-NO ladder structural validity. BUY_NO on a B-bracket
            # implicitly bets on either "stays below floor" OR "rockets above cap".
            # When rm is well below floor AND obs trend is flat/declining AND
            # peak is imminent, only the "stays low" path is realistic — but
            # Claude's P(NO) often counts both tails. Cap conviction to reflect
            # the single-tail reality.
            if decision.decision == "BUY_NO" and cand.bracket_kind == "B":
                _rm = packet.get("running_min_or_max")
                _trend = packet.get("obs_trend_30m") or 0.0
                _clk = packet.get("local_clock") or {}
                _lh = _clk.get("local_hour")
                _ph = (_clk.get("peak_hour_local")
                       if packet.get("series") == "KXHIGH"
                       else _clk.get("min_hour_local"))
                if (_rm is not None and cand.floor is not None and cand.cap is not None
                    and _lh is not None and _ph is not None):
                    _h_to_extreme = abs(_ph - _lh)
                    if packet.get("series") == "KXHIGH":
                        _single_tail = (_rm < cand.floor - 1.0
                                        and _trend <= 0.0
                                        and _h_to_extreme <= 2.0)
                    else:  # KXLOW
                        _single_tail = (_rm > cand.cap + 1.0
                                        and _trend >= 0.0
                                        and _h_to_extreme <= 2.0)
                    if _single_tail and decision.conviction > 0.85:
                        _conv_caps_applied.append(
                            f"single_tail_buy_no rm={_rm:.1f} trend={_trend:+.1f} "
                            f"h_to_extreme={_h_to_extreme:.1f} ({decision.conviction:.2f}→0.85)"
                        )
                        decision.conviction = 0.85

            # Cap 4 (2026-05-16): obs-self-defeat — when Claude's own key_risks
            # cite OBS-level evidence pointing INTO the YES window (rm/wethr
            # update past floor, statistical peak inside bracket, etc.), the
            # bet is structurally contradicted by Claude's own analysis. Cap
            # conviction to 0.82 → rejected by min_conviction_for_buy=0.83.
            # Patterns deliberately exclude forecast-based risks (NWS/model
            # citations) so we don't penalize legitimate contrarian-to-forecast
            # bets. Backtest 5/14-5/15: 6 fires, all 6 losers, 0 winners
            # blocked, +$89.54 net. Patterns hardened over the obs-only audit
            # in /tmp/c_refined.py.
            # _OBS_SELF_DEFEAT_RE defined at module level — re.compile no longer
            # re-runs on every LLM result.
            if decision.decision in ("BUY_NO", "BUY_YES") and decision.conviction > 0.82:
                _risks_joined = " ".join(decision.key_risks or [])
                _hit = _OBS_SELF_DEFEAT_RE.search(_risks_joined)
                if _hit:
                    _conv_caps_applied.append(
                        f"obs_self_defeat: \"{_hit.group(0)[:80]}\" "
                        f"({decision.conviction:.2f}→0.82)"
                    )
                    decision.conviction = 0.82

            if _conv_caps_applied:
                log.info("conviction caps on %s: %s",
                         cand.ticker, "; ".join(_conv_caps_applied))

            log.info(
                "  decision %s: %s (conv=%.2f size=%.2f, %.1fs)",
                cand.ticker, decision.decision, decision.conviction,
                decision.size_factor, decision.elapsed_sec,
            )

            # 2026-05-18: shadow-mode code-only decision path. Runs the same
            # decision logic in pure code (no LLM) and logs both for A/B.
            # NEVER raises — wrapped in try/except defensively. The LLM
            # decision above still drives the actual trade.
            try:
                from decide_entry_code import decide_entry_code
                _code_dec = decide_entry_code(packet)
                state.log_shadow_code_decision({
                    "ts": time.time(),
                    "ticker": cand.ticker,
                    "station": cand.station,
                    "mu_method": (packet.get("_edge_info") or {}).get("mu_method")
                                 or packet.get("mu_method"),
                    "mu_chosen": (packet.get("_edge_info") or {}).get("mu"),
                    "sigma_chosen": (packet.get("_edge_info") or {}).get("sigma"),
                    "yes_ask_c": packet.get("yes_ask_c"),
                    "no_ask_c": packet.get("no_ask_c"),
                    "code": {
                        "decision": _code_dec.decision,
                        "conviction": _code_dec.conviction,
                        "size_factor": _code_dec.size_factor,
                        "obs_anchor": _code_dec.obs_anchor,
                        "skip_reason": _code_dec.skip_reason,
                        "side_picked": _code_dec.side_picked,
                        "p_yes": _code_dec.p_yes,
                        "p_no": _code_dec.p_no,
                        "edge_pp": _code_dec.edge_pp,
                        "mu_used": _code_dec.mu_used,
                        "read": _code_dec.read[:300],
                    },
                    "llm": {
                        "decision": decision.decision,
                        "conviction": decision.conviction,
                        "size_factor": decision.size_factor,
                        "obs_anchor": decision.obs_anchor,
                        "obs_anchor_valid": decision.obs_anchor_valid,
                        "elapsed_sec": decision.elapsed_sec,
                    },
                    "agreement": _code_dec.decision == decision.decision,
                })
            except Exception as _e:
                log.exception("shadow code-only decision failed on %s: %s",
                              cand.ticker, _e)

            # Immediate processing — single-threaded since as_completed yields
            # one fut at a time to this for-loop body.
            guardrails.record_api_spend(rt.ctx, decision.est_cost_usd)
            decision_rec = {
                "kind": "entry_decision",
                "ticker": cand.ticker,
                "station": cand.station,
                "decision": decision.decision,
                "conviction": decision.conviction,
                "size_factor": decision.size_factor,
                "read": decision.read,
                "key_risks": decision.key_risks,
                "what_would_change_my_mind": decision.what_would_change_my_mind,
                # R2 2026-05-17: structured obs anchor + validator result
                "obs_anchor": decision.obs_anchor,
                "obs_anchor_valid": decision.obs_anchor_valid,
                "obs_anchor_reason": decision.obs_anchor_reason,
                "parse_ok": decision.parse_ok,
                "parse_error": decision.parse_error,
                "input_tokens": decision.input_tokens,
                "output_tokens": decision.output_tokens,
                "cached_input_tokens": decision.cached_input_tokens,
                "est_cost_usd": decision.est_cost_usd,
                "elapsed_sec": decision.elapsed_sec,
                "packet_summary": _packet_log_summary(packet),
            }
            state.log_decision(decision_rec)
            # Update + finalize the per-candidate row with LLM verdict.
            cand_rec["verdict"] = "llm_" + decision.decision.lower()
            cand_rec["llm_conviction"] = decision.conviction
            cand_rec["llm_size_factor"] = decision.size_factor
            cand_rec["llm_input_tokens"] = decision.input_tokens
            cand_rec["llm_output_tokens"] = decision.output_tokens
            cand_rec["llm_elapsed_sec"] = decision.elapsed_sec
            cand_rec["llm_read_short"] = (decision.read or "")[:500]
            cand_rec["llm_parse_ok"] = decision.parse_ok
            state.log_candidate(cand_rec)
            state.record_shared_decision(
                stage="entry", decision=decision.decision, ticker=cand.ticker,
                station=cand.station, floor=cand.floor, cap=cand.cap,
                llm_conviction=decision.conviction, llm_size_factor=decision.size_factor,
            )
            if not decision.parse_ok:
                guardrails.record_llm_failure(rt.ctx, g["llm_failure_pause_sec"])
                discord_send(
                    f"⚠️ **LLM ERROR** `{cand.ticker}` — "
                    f"parse_error={(decision.parse_error or 'unknown')[:200]}"
                )
                continue
            else:
                guardrails.record_llm_success(rt.ctx)

            if decision.decision == "SKIP":
                # CRITICAL: Discord notification MUST fire even if shadow
                # capture below crashes. Chris needs visibility on every
                # dispatched outcome — silent skips have caused multiple
                # incidents. Send Discord FIRST, then handle shadow logging
                # inside a try/except that can't kill the loop.
                discord_send(
                    f"⏭ **SKIP** `{cand.ticker}` "
                    f"(conv={decision.conviction:.2f}): {decision.read or ''}"
                )
                try:
                    # Shadow log high-conviction skips citing Rule #2 / wide-gap ceiling.
                    read_lc = (decision.read or "").lower()
                    if decision.conviction >= 0.78 and (
                        "rule #2" in read_lc or "25pp" in read_lc or "ceiling" in read_lc
                        or " gap" in read_lc and any(f"{n}pp" in read_lc for n in range(26, 100))
                    ):
                        rec = _capture_shadow_trade(cand, decision, packet, reason="rule2_skip")
                        if rec:
                            _log_shadow(rec)
                except Exception as _e:
                    log.exception("shadow capture failed on %s (Discord already sent): %s",
                                  cand.ticker, _e)
                continue

            # ── R2 2026-05-17: obs_anchor validation. Reject BUYs whose
            # declared obs_anchor field doesn't parse, isn't in the allowed
            # set, or doesn't match the packet value within tolerance.
            # Toggle via config.R2_ENFORCE_OBS_ANCHOR; in shadow mode the
            # trade proceeds but the would-be skip is logged.
            if not decision.obs_anchor_valid:
                _r2_reason = (
                    f"obs_anchor invalid: {decision.obs_anchor_reason or 'unknown'} "
                    f"(claimed={decision.obs_anchor!r})"
                )
                if config.R2_ENFORCE_OBS_ANCHOR:
                    log.info("BUY REJECTED %s: %s", cand.ticker, _r2_reason)
                    state.log_decision({"kind": "buy_rejected", "ticker": cand.ticker,
                                        "reason": _r2_reason})
                    discord_send(
                        f"⛔ **{decision.decision}** REJECTED `{cand.ticker}` — "
                        f"obs_anchor invalid "
                        f"({(decision.obs_anchor_reason or 'unknown')[:80]})"
                    )
                    continue
                else:
                    # Shadow: log but proceed
                    log.info("R2 SHADOW would-skip %s: %s",
                             cand.ticker, _r2_reason)
                    state.log_decision({"kind": "r2_shadow_skip",
                                        "ticker": cand.ticker,
                                        "reason": _r2_reason,
                                        "decision": decision.decision,
                                        "conviction": decision.conviction})

            # ── QUALITY + CORRELATION GATES (cycle-local) ──────────────────────
            # Each rejection here is a "tried to buy, blocked pre-execute" event.
            # Discord-notify so Chris sees the funnel, not just the fills.
            min_conv = g.get("min_conviction_for_buy", 0.0)
            min_size = g.get("min_size_factor_for_buy", 0.0)
            # NOTE: when execute_buy is called from _process_topups with
            # is_topup=True, the LLM dispatch above is skipped entirely, so
            # this branch (post-LLM gates) is never reached for top-ups.
            if decision.conviction < min_conv:
                log.info("BUY REJECTED %s: conviction %.2f < %.2f",
                         cand.ticker, decision.conviction, min_conv)
                state.log_decision({"kind": "buy_rejected", "ticker": cand.ticker,
                                    "reason": f"conviction {decision.conviction:.2f} < {min_conv}"})
                discord_send(
                    f"⛔ **{decision.decision}** REJECTED `{cand.ticker}` — "
                    f"conviction {decision.conviction:.2f} < {min_conv:.2f}"
                )
                continue
            if decision.size_factor < min_size:
                log.info("BUY REJECTED %s: size_factor %.2f < %.2f",
                         cand.ticker, decision.size_factor, min_size)
                state.log_decision({"kind": "buy_rejected", "ticker": cand.ticker,
                                    "reason": f"size_factor {decision.size_factor:.2f} < {min_size}"})
                discord_send(
                    f"⛔ **{decision.decision}** REJECTED `{cand.ticker}` — "
                    f"size_factor {decision.size_factor:.2f} < {min_size:.2f}"
                )
                continue
            if rt.cycle_buys_count >= g.get("max_buys_per_cycle", 999):
                log.info("BUY REJECTED %s: cycle cap %d reached",
                         cand.ticker, g["max_buys_per_cycle"])
                state.log_decision({"kind": "buy_rejected", "ticker": cand.ticker,
                                    "reason": f"cycle cap {g['max_buys_per_cycle']} reached"})
                discord_send(
                    f"⛔ **{decision.decision}** REJECTED `{cand.ticker}` — "
                    f"cycle cap {g['max_buys_per_cycle']} reached"
                )
                rec = _capture_shadow_trade(cand, decision, packet, reason="cycle_cap")
                if rec:
                    _log_shadow(rec)
                continue
            side_kind = "HIGH" if cand.series_prefix == "KXHIGH" else "LOW"
            corr_key = (cand.station, side_kind, cand.climate_day)
            # Post-LLM correlation cap removed — pre-LLM gate + dedupe now
            # cover this. Counter is still tracked below for bookkeeping.

            # ── F-RM-STALE (2026-05-16): refuse BUY when bot's running_max/min
            # disagrees with the live wethr 30-min window by >2°F in the side
            # that hurts the BUY. NY-B65.5 5/15: packet rm=55 but wethr+METAR
            # showed 61°F for 3h — LLM flagged the discrepancy in its risks
            # and bought anyway, lost $9.50. Backtest 5/14-5/15: 1 fire, +$9.69,
            # 0 false positives. Same-feed comparison (wethr_obs + temp_history
            # from shared cache), no new HTTP. Mirrored in _topup_is_safe.
            _rm_check = packet.get("running_min_or_max")
            if _rm_check is not None:
                _wo = packet.get("wethr_obs") or {}
                _hist = scache.get_temp_history(cand.station, lookback_sec=1800.0) or []
                _hist_t = [p.get("temp_f") for p in _hist if p.get("temp_f") is not None]
                _live_t = _wo.get("temp_f")
                if cand.series_prefix == "KXHIGH":
                    _cand_v = [t for t in (_live_t, _wo.get("high_f"), *_hist_t) if t is not None]
                    _live_ext = max(_cand_v) if _cand_v else None
                    _stale = (_live_ext is not None and (_live_ext - _rm_check) > 2.0)
                else:
                    _cand_v = [t for t in (_live_t, _wo.get("low_f"), *_hist_t) if t is not None]
                    _live_ext = min(_cand_v) if _cand_v else None
                    _stale = (_live_ext is not None and (_rm_check - _live_ext) > 2.0)
                if _stale:
                    _msg = (f"rm_stale: bot rm={_rm_check:.1f} vs live wethr "
                            f"extreme={_live_ext:.1f} (Δ>2°F)")
                    log.info("BUY REJECTED %s: %s", cand.ticker, _msg)
                    state.log_decision({"kind": "buy_rejected", "ticker": cand.ticker, "reason": _msg})
                    discord_send(
                        f"⛔ **{decision.decision}** REJECTED `{cand.ticker}` — {_msg}"
                    )
                    continue

            # ── F-TAIL-OVERSHOOT (2026-05-16, v2 2026-05-16): HIGH BUY_NO at/past
            # empirical peak hour (per-station per-(month or decile) from 3-yr
            # ASOS data, not hardcoded) where rm + tail_p75 reaches into
            # bracket. v1 used a station-level hardcoded peak_hour_local field;
            # v2 uses packet["empirical_peak_hour_local"] which is data-derived
            # at decile resolution when sample size supports it.
            # Backtest 5/14-5/15: 2 fires, +$39.29, 0 false positives.
            if (decision.decision == "BUY_NO" and cand.series_prefix == "KXHIGH"
                    and cand.floor is not None and _rm_check is not None):
                _clk2 = packet.get("local_clock") or {}
                _lh2 = _clk2.get("local_hour")
                _tb = packet.get("tail_band") or {}
                _peak_hr_local = packet.get("empirical_peak_hour_local")
                _tail_p75 = _tb.get("p75")
                _res = packet.get("tail_band_resolution") or "?"
                if (_lh2 is not None and _peak_hr_local is not None
                        and _tail_p75 is not None and _lh2 >= _peak_hr_local):
                    _projected = _rm_check + _tail_p75
                    if _projected >= (cand.floor - 0.5):
                        _msg2 = (f"tail_overshoot: rm={_rm_check:.1f} + "
                                 f"tail_p75={_tail_p75:.1f}°F = {_projected:.1f} "
                                 f">= floor-0.5={cand.floor-0.5:.1f} at "
                                 f"local_hour={_lh2:.2f} (emp_peak_hr="
                                 f"{_peak_hr_local}, {_res})")
                        log.info("BUY REJECTED %s: %s", cand.ticker, _msg2)
                        state.log_decision({"kind": "buy_rejected", "ticker": cand.ticker, "reason": _msg2})
                        discord_send(
                            f"⛔ **{decision.decision}** REJECTED `{cand.ticker}` — {_msg2}"
                        )
                        continue

            # ── F-TAIL-UNDERCOOL (2026-05-16, v2 2026-05-16): LOW mirror of
            # F-TAIL-OVERSHOOT. LOWT BUY_NO at/past empirical min hour
            # (data-derived per-(station, month or decile)) where rm −
            # tail_p75 reaches bracket from above. v1 used hardcoded
            # min_hour_local; v2 uses packet["empirical_min_hour_local"].
            # Backtest 5/14-5/15: 0 fires (no qualifying post-min trades);
            # shipped as defense-in-depth.
            if (decision.decision == "BUY_NO" and cand.series_prefix == "KXLOW"
                    and cand.cap is not None and _rm_check is not None):
                _clk3 = packet.get("local_clock") or {}
                _lh3 = _clk3.get("local_hour")
                _tlb = packet.get("tail_low_band") or {}
                _min_hr_local = packet.get("empirical_min_hour_local")
                _tail_low_p75 = _tlb.get("p75")
                _res_low = packet.get("tail_low_band_resolution") or "?"
                if (_lh3 is not None and _min_hr_local is not None
                        and _tail_low_p75 is not None and _lh3 >= _min_hr_local
                        and _rm_check > cand.cap + 0.5):
                    _projected_lo = _rm_check - _tail_low_p75
                    if _projected_lo <= (cand.cap + 0.5):
                        _msg3 = (f"tail_undercool: rm={_rm_check:.1f} − "
                                 f"tail_p75={_tail_low_p75:.1f}°F = {_projected_lo:.1f} "
                                 f"<= cap+0.5={cand.cap+0.5:.1f} at "
                                 f"local_hour={_lh3:.2f} (emp_min_hr="
                                 f"{_min_hr_local}, {_res_low})")
                        log.info("BUY REJECTED %s: %s", cand.ticker, _msg3)
                        state.log_decision({"kind": "buy_rejected", "ticker": cand.ticker, "reason": _msg3})
                        discord_send(
                            f"⛔ **{decision.decision}** REJECTED `{cand.ticker}` — {_msg3}"
                        )
                        continue

            # ── EXECUTE (immediate, while price still fresh) ──────────────────
            execute_buy(rt, cand, packet, decision)
            # After successful execute attempt, increment counters
            # (we count attempts, not just fills — keeps the cap honest
            # against insufficient-balance failures eating into our budget)
            rt.cycle_buys_count += 1
            rt.cycle_buys_by_station_side[corr_key] = (
                rt.cycle_buys_by_station_side.get(corr_key, 0) + 1
            )


def _max_affordable(balance_usd: float, ask_c: int) -> int:
    """Largest count that fits in available balance at given ask price."""
    if balance_usd is None or balance_usd <= 0 or ask_c <= 0:
        return 0
    return int((balance_usd * 100.0) // ask_c)


def execute_buy(rt: Runtime, cand: market_universe.Candidate,
                packet: dict, decision: judgment.EntryDecision,
                is_topup: bool = False) -> None:
    side = "no" if decision.decision == "BUY_NO" else "yes"
    packet_ask_c = packet.get("no_ask_c") if side == "no" else packet.get("yes_ask_c")
    if packet_ask_c is None:
        return

    # ── Refresh the ask just before sizing + placement. Claude latency is
    # 90-200s typical, during which the market moves and the packet's
    # snapshot goes stale. Orders placed at the stale ask rest as makers
    # because the live ask has moved 1-2c higher — observed pattern:
    # "BUY 15x no @ 73c → resting, filled=0 → cancelled 5s later".
    # Plus add 1c crossing buffer to absorb micro-movement during the order
    # RTT and ensure the order is a taker that fills immediately.
    # Prescreen (max_settled_threshold_cents=90) already filters out tickers
    # with ask ≥ 90c, so ask_c + 1 is safely below the max_price_cents cap.
    ask_c = packet_ask_c
    fresh_c: Optional[int] = None
    fresh_src = "stale"
    if USE_KALSHI_WS:
        try:
            bbo = kalshi_ws.get_bbo(cand.ticker)
        except Exception as _wse:
            bbo = None
            log.warning("kalshi_ws.get_bbo %s err: %s", cand.ticker, _wse)
        # WS BBO cache stores yes_bid + yes_ask only (binary-market convention).
        # NO ask = 1 - YES bid. NO bid = 1 - YES ask. See kalshi_ws._recompute_bbo.
        if bbo is not None:
            if side == "no":
                yb = bbo.get("yes_bid")
                ws_d = (1.0 - float(yb)) if yb is not None else None
            else:
                ws_d = bbo.get("yes_ask")
            if ws_d is not None:
                _c = int(round(float(ws_d) * 100))
                if _c > 0:
                    fresh_c = _c
                    fresh_src = "ws"
    if fresh_c is None:
        try:
            live = kalshi_client.get_market(cand.ticker)
            fresh_d = live.get("no_ask_dollars" if side == "no" else "yes_ask_dollars")
            if fresh_d is not None:
                _c = int(round(float(fresh_d) * 100))
                if _c > 0:
                    fresh_c = _c
                    fresh_src = "rest"
        except Exception as _e:
            log.warning("fresh ask refresh failed for %s: %s — using stale packet ask",
                        cand.ticker, _e)
    if fresh_c is not None:
        ask_c = fresh_c
    # 1c cross to ensure we're the taker, not a resting maker.
    ask_c = ask_c + 1
    log.info("ask refresh on %s: packet %dc → fresh %dc (via %s) → submit %dc (+1c cross)",
             cand.ticker, packet_ask_c, ask_c - 1, fresh_src, ask_c)

    # ── Drift sanity: if the market moved AGAINST us during Claude latency
    # such that the edge that justified the BUY no longer exists, SKIP.
    # Two checks (only for non-topup; top-ups have their own averaging-down
    # guard below):
    #   (a) Edge re-check: extract Claude's stated P(side) from the read,
    #       compute fresh edge = P_claude − fresh_ask/100, require ≥6pp.
    #       Loosened from 8pp on 2026-05-15 to match new scout-sweep gate.
    #   (b) Absolute drift cap (fallback when prob unextractable): if the
    #       fresh ask is ≥15c above packet ask, skip — that's a major
    #       repricing during latency, original analysis is stale.
    if not is_topup:
        claude_prob, _prob_src = _claude_prob_for_side(decision, packet, side)
        market_implied = ask_c / 100.0
        if claude_prob is not None:
            fresh_edge_pp = (claude_prob - market_implied) * 100.0
            if fresh_edge_pp < 6.0:
                log.info("BUY SKIP %s: market drifted — fresh edge %.1fpp < 6pp "
                         "(packet %dc → submit %dc, prob %.2f from %s)",
                         cand.ticker, fresh_edge_pp, packet_ask_c, ask_c,
                         claude_prob, _prob_src)
                state.log_decision({"kind": "buy_rejected", "ticker": cand.ticker,
                    "reason": f"market_drift: edge {fresh_edge_pp:.1f}pp < 6pp "
                              f"(packet {packet_ask_c}c → submit {ask_c}c)"})
                discord_send(
                    f"⛔ **{decision.decision}** SKIP `{cand.ticker}` — "
                    f"market drifted during Claude latency. "
                    f"Packet ask {packet_ask_c}c → fresh+1c {ask_c}c. "
                    f"Edge collapsed to {fresh_edge_pp:.1f}pp (need ≥6pp). "
                    f"Prob {claude_prob:.2f} ({_prob_src})."
                )
                return
        else:
            # Fallback: can't extract prob — guard on raw drift.
            drift_c = ask_c - packet_ask_c
            if drift_c >= 16:  # 15c fresh-vs-packet + 1c cross = 16c
                log.info("BUY SKIP %s: fresh ask drifted +%dc above packet "
                         "(packet %dc → submit %dc); prob unextractable, refusing",
                         cand.ticker, drift_c, packet_ask_c, ask_c)
                state.log_decision({"kind": "buy_rejected", "ticker": cand.ticker,
                    "reason": f"market_drift: packet {packet_ask_c}c → submit {ask_c}c "
                              f"(+{drift_c}c), no prob in read"})
                discord_send(
                    f"⛔ **{decision.decision}** SKIP `{cand.ticker}` — "
                    f"ask drifted +{drift_c}c during Claude latency "
                    f"(packet {packet_ask_c}c → {ask_c}c). "
                    f"Prob unextractable, can't verify edge — skipping."
                )
                return

    # ── Top-up safety: don't average DOWN into a collapsing position.
    # Top-ups skip the LLM and re-use the entry-time conviction. If the
    # market has moved >10% against our side since entry, that stored
    # conviction is stale — adding more risks throwing good money after
    # bad as the position collapses toward worthless. Worst case avoided:
    # original 1-contract fill at 17c, market drops to 1c, top-up buys
    # 400+ contracts that settle at 0.
    if is_topup:
        pos = rt.positions.get(cand.ticker) or {}
        entry_price = pos.get("entry_price")
        if entry_price and entry_price > 0:
            entry_c = int(round(entry_price * 100))
            min_safe_c = int(entry_c * 0.90)  # 10% below entry
            if ask_c < min_safe_c:
                log.info("TOPUP SKIP %s: %s_ask %dc < %dc (90%% of entry %dc) — "
                         "position weakening, not averaging down",
                         cand.ticker, side, ask_c, min_safe_c, entry_c)
                state.log_decision({"kind": "buy_rejected", "ticker": cand.ticker,
                                    "reason": f"topup_drift: ask {ask_c}c < 90% of entry {entry_c}c"})
                discord_send(
                    f"⛔ **TOPUP BLOCKED** `{cand.ticker}` — "
                    f"current ask {ask_c}c < {min_safe_c}c (90% of entry {entry_c}c). "
                    f"Market moved against position; not averaging down."
                )
                return

    # ── Balance — needed for both paths. Fetch first.
    balance_usd = kalshi_client.get_balance_cached()
    if balance_usd is None:
        log.warning("BUY SKIP %s: no balance fetched yet (cold start)", cand.ticker)
        state.log_decision({"kind": "buy_rejected", "ticker": cand.ticker,
                            "reason": "no_balance_yet"})
        discord_send(
            f"⛔ **{decision.decision}** SKIP `{cand.ticker}` — "
            f"balance unavailable (cold start). "
            f"conv={decision.conviction:.2f}"
        )
        return

    min_buy = config.GUARDRAILS.get("min_buy_usd", 0.0)
    if cand.ticker.startswith("KXLOW"):
        # 2026-05-23: LOW $1-probe. The global $1 min_buy_usd floor rejects a
        # 1-contract LOW buy (~$0.60-0.80) at the $1 cap ("reachable $0.60 <
        # floor $1.00"), so LOW never places an order. Use the LOW-specific
        # floor (PUSH_MIN_BUY_USD_LOW=$0.40) so the scout passes.
        min_buy = float(getattr(config, "PUSH_MIN_BUY_USD_LOW", min_buy))
    max_price_cents = int(config.GUARDRAILS.get("max_price_cents", 90))

    # ── Sizing path: TOP-UP uses simple gap-to-min_buy; FRESH uses scout.
    if is_topup:
        cur_cost = rt.ctx.ticker_total_cost_usd.get(cand.ticker, 0.0)
        bet_usd = max(0.0, min_buy - cur_cost)
        if bet_usd <= 0:
            log.info("top-up SKIP %s: already at/above min_buy_usd", cand.ticker)
            return
        intended_cnt = max(1, int(bet_usd / (ask_c / 100.0)))
        max_aff = _max_affordable(balance_usd, ask_c)
        if max_aff < 1:
            log.info("TOPUP SKIP %s: wallet $%.2f < ask %dc",
                     cand.ticker, balance_usd, ask_c)
            state.log_decision({"kind": "buy_rejected", "ticker": cand.ticker,
                                "reason": f"balance ${balance_usd:.2f} < ask {ask_c}c"})
            discord_send(
                f"⛔ **TOPUP** SKIP `{cand.ticker}` — "
                f"wallet ${balance_usd:.2f} < ask {ask_c}c"
            )
            return
        cnt = min(intended_cnt, max_aff)
        clamped = (cnt < intended_cnt)
        actual_cost = cnt * (ask_c / 100.0)
    else:
        # FRESH BUY — scout the orderbook, plan the smallest sweep that
        # reaches min_buy at edge ≥ 6pp. If infeasible, SKIP cleanly with
        # detailed Discord reason. This replaces the prior "size by intent,
        # hope it fills" path that left tiny partial positions when the
        # book was thinner than the ask price suggested (May 15 incident:
        # AUS-B90.5 BUY 42x → filled 1x → $0.46 dust).
        if side == "no":
            side_cap = config.GUARDRAILS.get("max_bet_no_usd",
                                              config.GUARDRAILS.get("max_bet_usd", 15.0))
        else:
            side_cap = config.GUARDRAILS.get("max_bet_yes_usd",
                                              config.GUARDRAILS.get("max_bet_usd", 15.0))

        # HIGH-series tighter cap (2026-05-16): after string of forecast-anchored
        # BUY_NO losses on KXHIGH brackets, cap HIGH-series entries at the lower
        # max_bet_high_series_usd. LOW series unchanged.
        if cand.ticker.startswith("KXHIGH"):
            high_cap = config.GUARDRAILS.get("max_bet_high_series_usd")
            if high_cap is not None and high_cap < side_cap:
                side_cap = high_cap
        # LOW-series tighter cap (2026-05-16 evening): symmetric to HIGH while
        # validating nn_match k-NN heating-curve μ as the primary projection.
        if cand.ticker.startswith("KXLOW"):
            low_cap = config.GUARDRAILS.get("max_bet_low_series_usd")
            if low_cap is not None and low_cap < side_cap:
                side_cap = low_cap

        # Pre-flight infeasibility (avoid an API call when impossible).
        if side_cap < min_buy:
            log.warning("BUY SKIP %s: config side_cap $%.2f < min_buy $%.2f",
                        cand.ticker, side_cap, min_buy)
            state.log_decision({"kind": "buy_rejected", "ticker": cand.ticker,
                                "reason": f"side_cap ${side_cap} < min_buy ${min_buy}"})
            discord_send(
                f"⛔ **{decision.decision}** SKIP `{cand.ticker}` — "
                f"config side_cap ${side_cap:.2f} < min_buy ${min_buy:.2f}. "
                f"Misconfig — fresh buy impossible by policy."
            )
            return
        if balance_usd < min_buy:
            log.info("BUY SKIP %s: wallet $%.2f < min_buy $%.2f",
                     cand.ticker, balance_usd, min_buy)
            state.log_decision({"kind": "buy_rejected", "ticker": cand.ticker,
                                "reason": f"balance ${balance_usd:.2f} < min_buy ${min_buy:.2f}"})
            discord_send(
                f"⛔ **{decision.decision}** SKIP `{cand.ticker}` — "
                f"wallet ${balance_usd:.2f} < min_buy ${min_buy:.2f}. "
                f"Can't afford a min-buy position."
            )
            return

        # Need Claude's prob for the scout's edge gate.
        scout_prob, prob_src = _claude_prob_for_side(decision, packet, side)
        if scout_prob is None:
            log.info("BUY SKIP %s: no prob signal (read has no P, edge_info missing)",
                     cand.ticker)
            state.log_decision({"kind": "buy_rejected", "ticker": cand.ticker,
                                "reason": "no_prob_signal"})
            discord_send(
                f"⛔ **{decision.decision}** SKIP `{cand.ticker}` — "
                f"no probability signal available "
                f"(Claude read had no P(NO)/P(YES), no edge_info fallback). "
                f"Can't verify edge — skipping."
            )
            return

        target_cost = side_cap * decision.size_factor
        scout = _scout_sweep_plan(
            ticker=cand.ticker, side=side, claude_prob=scout_prob,
            target_cost=target_cost, floor_cost=min_buy,
            side_cap=side_cap, balance_usd=balance_usd,
            max_price_cents=max_price_cents, min_edge_pp=6.0,
        )

        # Telemetry: log every scout (success or skip) for retrospective analysis.
        try:
            state.log_scout({
                "ts": rt.ctx.now_utc, "ticker": cand.ticker, "side": side,
                "claude_decision": decision.decision,
                "claude_conviction": decision.conviction,
                "claude_size_factor": decision.size_factor,
                "claude_prob": round(float(scout_prob), 4),
                "prob_source": prob_src,
                "target_cost": round(target_cost, 4),
                "floor_cost": min_buy, "side_cap": side_cap,
                "balance_usd": round(balance_usd, 2),
                "min_edge_pp": 6.0,
                "packet_ask_c": packet_ask_c,
                "scan": [[p, s, e] for p, s, e in scout["scan"]],
                "best_offers": [[p, c] for p, c in scout["best_offers"]],
                "plan_ok": scout["ok"],
                "skip_reason": scout["skip_reason"],
                "plan_qty": scout["qty"],
                "plan_max_price_c": scout["max_price_c"],
                "plan_expected_cum_cost": scout["expected_cum_cost"],
                "plan_expected_avg_c": scout["expected_avg_c"],
                "plan_edge_at_max_pp": scout["edge_at_max_pp"],
            })
        except Exception as _e:
            log.warning("log_scout failed for %s: %s", cand.ticker, _e)

        if not scout["ok"]:
            log.info("BUY SKIP %s: scout — %s", cand.ticker, scout["skip_reason"])
            state.log_decision({"kind": "buy_rejected", "ticker": cand.ticker,
                                "reason": f"scout: {scout['skip_reason']}"})
            offers_str = ", ".join(f"{p}c×{c}" for p, c in scout["best_offers"][:4])
            if not offers_str:
                offers_str = "(empty)"
            discord_send(
                f"⛔ **{decision.decision}** SKIP `{cand.ticker}` — "
                f"{scout['skip_reason']}\n"
                f"Best offers: {offers_str}. "
                f"Prob {scout_prob:.2f} ({prob_src}), "
                f"target ≥${min_buy:.2f}, edge floor 6pp."
            )
            return

        # Plan accepted — use scout-computed qty + max_price.
        cnt = scout["qty"]
        ask_c = scout["max_price_c"]
        intended_cnt = cnt
        clamped = False
        actual_cost = scout["expected_cum_cost"]
        log.info("BUY SCOUT %s: plan %dx max_price=%dc avg≈%.0fc cost≈$%.2f "
                 "edge_at_max=%.1fpp (prob %.2f from %s)",
                 cand.ticker, cnt, ask_c, scout["expected_avg_c"],
                 actual_cost, scout["edge_at_max_pp"], scout_prob, prob_src)

    bd = guardrails.BuyDecision(
        ticker=cand.ticker,
        side=side,
        count=cnt,
        price_cents=ask_c,
        cost_usd=actual_cost,
        seconds_to_close=packet.get("seconds_to_close") or 0,
    )
    ok, reason = guardrails.check_buy(rt.ctx, bd, config.GUARDRAILS)
    if not ok:
        log.info("BUY REJECTED %s: %s", cand.ticker, reason)
        state.log_decision({"kind": "buy_rejected", "ticker": cand.ticker, "reason": reason})
        discord_send(
            f"⛔ **{decision.decision}** REJECTED `{cand.ticker}` "
            f"({cnt}x @ {ask_c}c, ${actual_cost:.2f}) — guardrail: {reason[:120]}"
        )
        rec = _capture_shadow_trade(cand, decision, packet, reason=f"guardrail:{reason[:60]}")
        if rec:
            _log_shadow(rec)
        return

    # ── Discord: ALWAYS notify on every buy attempt, success or fail.
    discord_send(
        f"🎯 **{decision.decision}** ATTEMPT `{cand.ticker}` "
        f"{cnt}x @ {ask_c}c (${actual_cost:.2f}) — "
        f"conv={decision.conviction:.2f} size={decision.size_factor:.2f} "
        f"wallet=${balance_usd:.2f}"
        + (f" (clamped from {intended_cnt}x)" if clamped else "")
    )

    # Claim BEFORE placing the order so V2 max + V2 min reconcile can't
    # race-adopt the fill mid-cycle.
    state.claim_ticker(cand.ticker)

    # ── Submit. On insufficient_balance (race with a sibling bot's order),
    # refresh balance and retry once with the new clamp.
    order_resp = kalshi_client.place_buy(cand.ticker, side, cnt, ask_c)
    if not order_resp.get("ok"):
        err_code = order_resp.get("error_code") or "unknown"
        err_msg = order_resp.get("error_msg") or ""
        if err_code == "insufficient_balance":
            # Refresh balance, recompute affordability, retry once if any room
            kalshi_client.invalidate_balance_cache()
            bal2 = kalshi_client.get_balance_cached(force_refresh=True)
            max_aff2 = _max_affordable(bal2 or 0.0, ask_c)
            if max_aff2 < 1:
                state.release_ticker(cand.ticker)
                discord_send(
                    f"❌ **{decision.decision}** FAILED `{cand.ticker}` "
                    f"({cnt}x @ {ask_c}c) — insufficient_balance, "
                    f"refresh shows wallet=${bal2 or 0.0:.2f} < ask {ask_c}c. Giving up."
                )
                return
            cnt2 = min(cnt, max_aff2)
            actual_cost2 = cnt2 * (ask_c / 100.0)
            log.info("BUY RETRY %s after insufficient_balance: clamped %d→%d "
                     "(wallet $%.2f)", cand.ticker, cnt, cnt2, bal2)
            discord_send(
                f"♻️ **{decision.decision}** RETRY `{cand.ticker}` "
                f"{cnt2}x @ {ask_c}c (${actual_cost2:.2f}) — "
                f"first attempt insufficient_balance, wallet now ${bal2:.2f}"
            )
            order_resp = kalshi_client.place_buy(cand.ticker, side, cnt2, ask_c)
            if not order_resp.get("ok"):
                state.release_ticker(cand.ticker)
                err2 = order_resp.get("error_code") or "unknown"
                msg2 = order_resp.get("error_msg") or ""
                discord_send(
                    f"❌ **{decision.decision}** FAILED `{cand.ticker}` "
                    f"({cnt2}x @ {ask_c}c) — retry rejected: {err2}: {msg2[:120]}"
                )
                return
            # Retry succeeded — pick up the new size for downstream bookkeeping.
            cnt = cnt2
            actual_cost = actual_cost2
        else:
            state.release_ticker(cand.ticker)
            discord_send(
                f"❌ **{decision.decision}** FAILED `{cand.ticker}` "
                f"({cnt}x @ {ask_c}c) — {err_code}: {err_msg[:120]}"
            )
            return

    order_id = order_resp.get("order_id")
    filled = int(order_resp.get("filled") or 0)
    status = order_resp.get("status", "?")
    if not order_id:
        log.warning("BUY %s placement returned no order_id", cand.ticker)
        state.release_ticker(cand.ticker)
        discord_send(
            f"❌ **{decision.decision}** FAILED `{cand.ticker}` "
            f"({cnt}x @ {ask_c}c) — no order_id in response (status={status})"
        )
        return
    # If place response didn't already report filled, poll briefly.
    if filled <= 0:
        status, filled = kalshi_client.wait_for_fill(order_id, cnt)
    if filled <= 0:
        log.info("BUY %s not filled (status=%s)", cand.ticker, status)
        if not config.DRY_RUN:
            kalshi_client.cancel_order(order_id)
        state.release_ticker(cand.ticker)
        discord_send(
            f"⚠️ **{decision.decision}** NOT FILLED on `{cand.ticker}` "
            f"({cnt}x @ {ask_c}c, status={status}) — order cancelled. "
            f"conv={decision.conviction:.2f}: {decision.read}"
        )
        return
    # Partial fill: cancel the resting remainder so we don't get filled at
    # a worse price later if the market moves against us. The bot may
    # re-attempt this ticker next cycle (gated by min_buy_usd cumulative).
    if filled < cnt and not config.DRY_RUN:
        try:
            kalshi_client.cancel_order(order_id)
            log.info("BUY %s partial fill %d/%d — cancelled resting remainder",
                     cand.ticker, filled, cnt)
        except Exception as e:
            log.warning("cancel_order for partial remainder %s failed: %s",
                        cand.ticker, e)
    filled_cost = filled * (ask_c / 100.0)
    rt.hourly_buys_attempted += 1
    rt.hourly_buys_filled += 1 if filled > 0 else 0
    rt.hourly_buy_cost_usd += filled_cost

    # ── Dust-tolerance check (fresh buys only; top-ups are already by
    # definition adding to sub-min positions). Scout planned ≥ min_buy at
    # acceptable edge, but a race during the order RTT can leave us with
    # a partial fill below the floor. Three bands:
    #
    #   filled_cost ≥ min_buy_usd      → SUCCESS, normal record
    #   ≥ 80% × min_buy ≤ . < min_buy  → "close-call dust" — keep position,
    #                                      next-cycle top-up handles the gap
    #   filled_cost < 80% × min_buy    → "true dust" — log + Discord warn.
    #                                      Sell-back path is DESIGNED but
    #                                      DISABLED for now (commented below)
    #                                      so we can OBSERVE dust frequency
    #                                      before paying spread to clean it up.
    if not is_topup and filled > 0:
        dust_floor = min_buy * 0.80
        if filled_cost < dust_floor:
            log.warning("DUST %s: filled %dx@%dc=$%.2f < %.0f%% of min_buy "
                        "$%.2f (=$%.2f). Position held (sell-back disabled).",
                        cand.ticker, filled, ask_c, filled_cost,
                        80, min_buy, dust_floor)
            discord_send(
                f"⚠️ **DUST** `{cand.ticker}` — filled {filled}x@{ask_c}c=${filled_cost:.2f} "
                f"< 80% of min_buy (${dust_floor:.2f}). "
                f"Position held; sell-back is disabled — observing dust frequency."
            )
            # ────────────────────────────────────────────────────────────
            # DISABLED 2026-05-15 — sell dust back to enforce min_buy strictly.
            # Re-enable after a few days of observation if dust events are
            # frequent and the held positions create real drag. Each event
            # costs ~1-2c × filled = a few pennies in spread.
            #
            # try:
            #     fresh = kalshi_client.get_market(cand.ticker)
            #     bid_d = fresh.get("no_bid_dollars" if side == "no" else "yes_bid_dollars")
            #     if bid_d is not None and float(bid_d) > 0:
            #         bid_c = int(round(float(bid_d) * 100))
