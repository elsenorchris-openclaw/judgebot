def _season(month: int) -> str:
    if month in (12, 1, 2): return "DJF"
    if month in (3, 4, 5): return "MAM"
    if month in (6, 7, 8): return "JJA"
    return "SON"


def cell_mae(station: str, month: int, local_hour, side: str):
    """Look up historical MAE for a cell. Returns None if unknown.
    station may be ICAO (KXXX) or bare (XXX); side is "high"/"low".
    Hour is floored to int. Missing cell -> None (caller should not gate)."""
    if station is None or local_hour is None or side is None:
        return None
    sta = station[1:] if (isinstance(station, str) and station.startswith("K")
                          and len(station) > 3) else station
    try:
        hr = int(float(local_hour)) % 24
    except (TypeError, ValueError):
        return None
    return CELL_MAE.get((sta, _season(int(month)), hr, side))
